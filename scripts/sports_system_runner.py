def game_matches_row(game: dict[str, Any], row: dict[str, Any]) -> bool:
    gid = str(row.get("Game ID") or row.get("game_id") or "")
    if gid and gid == str(game.get("event_id") or game.get("id") or ""):
        return True
    home = row.get("Home Team") or row.get("home_team")
    away = row.get("Away Team") or row.get("away_team")
    if home or away:
        return bool(team_aliases(home) & team_aliases(game.get("home_team")) and team_aliases(away) & team_aliases(game.get("away_team")))
    team = row.get("Team")
    if team and (team_aliases(team) & (team_aliases(game.get("home_team")) | team_aliases(game.get("away_team")))):
        return True
    text = " ".join(str(v or "") for v in row.values())
    return bool((str(game.get("home_team") or "").lower() in text.lower()) or (str(game.get("away_team") or "").lower() in text.lower()))


def odds_profit(result: str, units: float, odds: Any = None) -> float:
    if result == "LOSS":
        return round(units * -1, 3)
    if result in {"PUSH", "VOID", "PENDING", "MANUAL REVIEW"}:
        return 0.0
    price = to_float(odds)
    if price is None or price == 0:
        return round(units * 0.909, 3)
    if price > 0:
        return round(units * (price / 100.0), 3)
    return round(units * (100.0 / abs(price)), 3)


def units_from_prop_reason(row: dict[str, Any]) -> float:
    reason = str(row.get("Reasoning") or "")
    for token in reason.replace(";", " ").split():
        if token.startswith("units="):
            return float(to_float(token.split("=", 1)[1]) or 0) or 1.0
    return 1.0


