def sync_master_clv_fields(date: str, sport_label: str, clv_rows: list[dict[str, Any]]) -> int:
    """Update CLV-related Pick History columns after the CLV tracker captures closing lines."""
    if not clv_rows:
        return 0
    wb, master = master_pnl_workbook()
    ph = wb["Pick History"]
    cols = ensure_ws_columns(ph, RESULT_HEADERS)
    updated = 0
    for r in range(2, ph.max_row + 1):
        if str(ph.cell(r, cols["Date"]).value or "")[:10] != date:
            continue
        if str(ph.cell(r, cols["Sport"]).value or "").upper() != sport_label:
            continue
        ref = str(ph.cell(r, cols["Pick Ref"]).value or "")
        pick_type = str(ph.cell(r, cols["Pick Type"]).value or "").upper()
        row = matching_clv_row(clv_rows, ref, pick_type, ph.cell(r, cols["Pick"]).value, ph.cell(r, cols["Player/Team"]).value)
        if not row or row.get("CLV") in (None, ""):
            continue
        opening = row.get("Morning Line") if row.get("Morning Line") not in (None, "") else row.get("Our Line")
        closing = row.get("Closing Line")
        move = line_movement_amount(opening, closing)
        ph.cell(r, cols["CLV"]).value = row.get("CLV")
        ph.cell(r, cols["Opening Line"]).value = opening
        ph.cell(r, cols["Closing Line"]).value = closing
