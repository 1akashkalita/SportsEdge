def safe_print(line: str, *, file: Any = None) -> None:
    """Print operational output without letting a closed cron pipe fail the task."""
    try:
        print(line, file=file or sys.stdout)
    except BrokenPipeError:
        try:
            sys.stdout = open(os.devnull, "w")
        except Exception:
            pass


def log(msg: str) -> None:
    ensure_dirs()
    line = f"[{now_iso()}] {msg}"
    with RUN_LOG.open("a") as f:
        f.write(line + "\n")
    # Accumulate log lines for the single end-of-task Obsidian summary sync in main()'s finally.
    _task_log_lines.append(line)
    safe_print(line)


def env_value(key: str) -> str | None:
    """Read a value from process env, then Hermes .env without leaking secrets."""
    value = os.environ.get(key)
    if value:
        return value.strip().strip('"').strip("'")
    if not HERMES_ENV.exists():
        return None
    for line in HERMES_ENV.read_text().splitlines():
        stripped = line.strip()
        if not stripped or stripped.startswith("#") or "=" not in stripped:
            continue
        k, v = stripped.split("=", 1)
        if k.strip() == key:
            return v.strip().strip('"').strip("'") or None
    return None


def send_telegram(message: str, retries: int = 2, backoff: int = 5) -> bool:
    """Send a real Telegram alert to the configured Hermes home channel without crashing tasks."""
    token = env_value("TELEGRAM_BOT_TOKEN")
    chat_id = env_value("TELEGRAM_HOME_CHANNEL") or env_value("TELEGRAM_CHAT_ID")
    thread_id = env_value("TELEGRAM_CRON_THREAD_ID") or env_value("TELEGRAM_HOME_CHANNEL_THREAD_ID")
    if not token or not chat_id:
        log("Telegram alert skipped: TELEGRAM_BOT_TOKEN or TELEGRAM_HOME_CHANNEL missing")
        return False
    # Per-invocation circuit-breaker: drop remaining alerts fast when Telegram is unreachable.
    if _telegram_breaker["tripped"]:
        _telegram_breaker["suppressed"] += 1
        return False
    payload: dict[str, Any] = {"chat_id": chat_id, "text": message, "disable_web_page_preview": True}
    if thread_id:
        payload["message_thread_id"] = thread_id
    delay = backoff
    for attempt in range(retries):
        try:
            r = requests.post(TELEGRAM_API.format(token=token), json=payload, timeout=10)
            if r.status_code == 200:
                log("Telegram alert sent")
                _telegram_breaker["consecutive_failures"] = 0
                return True
            log(f"Telegram alert failed attempt {attempt+1}/{retries}: status={r.status_code} body={r.text[:300]}")
        except requests.exceptions.RequestException as e:
            log(f"Telegram alert failed attempt {attempt+1}/{retries}: {e}")
        except Exception as e:
            log(f"Telegram alert failed attempt {attempt+1}/{retries}: {e}")
        if attempt < retries - 1:
            time.sleep(delay)
    log("Telegram alert failed after all retries; continuing without crashing task")
    _telegram_breaker["consecutive_failures"] += 1
    if _telegram_breaker["consecutive_failures"] >= 3 and not _telegram_breaker["tripped"]:
        _telegram_breaker["tripped"] = True
        log("Telegram circuit-breaker tripped — alerts suppressed — Telegram unreachable")
    return False


