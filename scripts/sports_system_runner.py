def sync_master_and_bankroll(date: str, newly_graded: list[dict[str, Any]]) -> dict[str, Any]:
    wb, master = master_pnl_workbook()
    ph = wb["Pick History"]
    ph_cols = ensure_ws_columns(ph, RESULT_HEADERS)
    for row in newly_graded:
        remove_master_pick_history_ref(ph, date, row["sport"], row["ref"])
        ph.append([None] * len(RESULT_HEADERS))
        r = ph.max_row
        values = {h: row.get(h) for h in RESULT_HEADERS}
        # Backward-compatible aliases for any legacy callers that still pass compact result dicts.
        values.update({
            "Date": values.get("Date") or date,
            "Sport": values.get("Sport") or row.get("sport"),
            "Game": values.get("Game") or row.get("game", ""),
            "Pick Ref": values.get("Pick Ref") or row.get("ref"),
            "Result": values.get("Result") or row.get("result"),
            "Actual": values.get("Actual") if values.get("Actual") not in (None, "") else row.get("actual"),
            "Units": values.get("Units") if values.get("Units") not in (None, "") else row.get("units"),
            "PnL": values.get("PnL") if values.get("PnL") not in (None, "") else row.get("pnl"),
            "Graded At": values.get("Graded At") or row.get("graded_at"),
            "Notes": values.get("Notes") or row.get("note", ""),
        })
        for h, v in values.items():
            if h in ph_cols:
                ph.cell(r, ph_cols[h]).value = v
    daily_rows: dict[str, list[dict[str, Any]]] = {"NBA": [], "MLB": []}
    for vals in ph.iter_rows(min_row=2, values_only=True):
        d = dict(zip([ph.cell(1, c).value for c in range(1, ph.max_column + 1)], vals))
        if str(d.get("Date") or "")[:10] == date:
            daily_rows.setdefault(str(d.get("Sport") or ""), []).append(d)
    for sheet in ["Daily Log", "Bankroll Chart Data"]:
        remove_rows_for_date(wb[sheet], date)
    for sport_label, rows in daily_rows.items():
        wins = len([r for r in rows if r.get("Result") == "WIN"])
        losses = len([r for r in rows if r.get("Result") == "LOSS"])
        pushes = len([r for r in rows if r.get("Result") == "PUSH"])
        units = round(sum(float(to_float(r.get("Units")) or 0) for r in rows if r.get("Result") not in {"PENDING", "MANUAL REVIEW"}), 3)
        pnl = round(sum(float(to_float(r.get("PnL")) or 0) for r in rows if r.get("Result") not in {"PENDING", "MANUAL REVIEW"}), 3)
        wb["Daily Log"].append([date, sport_label, wins, losses, pushes, units, pnl, "", "live game grading/reconciliation"])
    try:
        bankroll = json.loads(BANKROLL.read_text()) if BANKROLL.exists() else {}
    except Exception:
        bankroll = {}
    starting = float(bankroll.get("starting_bankroll", 100) or 100)
    total_profit = 0.0
    total_units = 0.0
    for vals in wb["Daily Log"].iter_rows(min_row=2, values_only=True):
        total_units += float(to_float(vals[5] if len(vals) > 5 else 0) or 0)
        total_profit += float(to_float(vals[6] if len(vals) > 6 else 0) or 0)
    current = round(starting + total_profit, 3)
    roi = round((total_profit / total_units) * 100, 2) if total_units else 0
    bankroll.update({
        "starting_bankroll": starting,
        "current_bankroll": current,
        "total_units_bet_lifetime": round(total_units, 3),
        "overall_profit_loss": round(total_profit, 3),
        "roi_percentage_current": roi,
        "last_graded_date": date,
        "last_updated": now_iso(),
        "updated_at": now_iso(),
    })
    for row in wb["Daily Log"].iter_rows(min_row=2):
        if str(row[0].value or "")[:10] == date:
            row[7].value = current
    wb["Bankroll Chart Data"].append([date, current, roi, now_iso()])
    all_today = []
    for rows in daily_rows.values():
        for r in rows:
            all_today.append({"result": r.get("Result"), "units": r.get("Units"), "pnl": r.get("PnL")})
    refresh_performance_breakdown(wb, bankroll, all_today)
    save_workbook_atomic(wb, master)
    BANKROLL.write_text(json.dumps(bankroll, indent=2) + "\n")
    flat = []
    for sport_label, rows in daily_rows.items():
        for r in rows:
            flat.append({"sport": sport_label, "ref": r.get("Pick Ref"), "result": r.get("Result"), "units": r.get("Units"), "pnl": r.get("PnL"), "note": r.get("Notes"), "actual": r.get("Actual"), "platform": r.get("Platform") or ""})
    day_pnl = round(sum(float(to_float(r.get("pnl")) or 0) for r in flat), 3)
    obsidian_update_results_section("nba", [r for r in flat if r["sport"] == "NBA"], date, current, sum(float(to_float(r.get("pnl")) or 0) for r in flat if r["sport"] == "NBA"), None)
    obsidian_update_results_section("mlb", [r for r in flat if r["sport"] == "MLB"], date, current, sum(float(to_float(r.get("pnl")) or 0) for r in flat if r["sport"] == "MLB"), None)
    obsidian_update_bankroll_files(date, bankroll, flat, current, roi, day_pnl)
    return {"bankroll": bankroll, "current": current, "roi": roi, "day_pnl": day_pnl, "daily_rows": flat, "master_pnl": str(master)}


def grade_game_in_workbook(sport: str, game: dict[str, Any], date: str | None = None, dry_run: bool = False) -> dict[str, Any]:
    date = date or today_str()
    sport_label = sport.upper()
    path = ensure_workbook(sport, date)
    wb = safe_load_workbook(path)
    picks_ws = wb["Picks"]
    props_ws = wb["Props"]
    parlays_ws = wb["Correlated Parlays"]
    results_ws = wb["Results"]
    clv_ws = wb["CLV Tracker"] if "CLV Tracker" in wb.sheetnames else None
    ensure_ws_columns(results_ws, RESULT_HEADERS)
    clv_rows = clv_lookup_from_sheet(clv_ws, date, sport_label)
    already = existing_result_refs(results_ws, date, sport_label)
    game_label = f"{game.get('away_team')} @ {game.get('home_team')}"
    is_void = game.get("status_name") in {"STATUS_POSTPONED", "STATUS_SUSPENDED"} or game.get("status") == "void"
    final_game = dict(game)
    final_game["completed"] = bool(game.get("completed") or game.get("status") == "final")
    player_stats = {} if is_void else espn_player_stats_by_event(sport, str(game.get("event_id") or game.get("id") or ""))
    graded: list[dict[str, Any]] = []
    manual_reviews: list[dict[str, Any]] = []

    pick_headers = [c.value for c in picks_ws[1]]
    for vals in picks_ws.iter_rows(min_row=2, values_only=True):
        if not vals or str(vals[0] or "")[:10] != date:
            continue
        row = dict(zip(pick_headers, vals))
        if not game_matches_row(game, row):
            continue
        pick_type = str(row.get("Pick Type") or "").upper()
        if pick_type == "PROP":
            continue
        ref = f"{pick_type}:{row.get('Selection')}"
        if ref in already:
            continue
        units = float(to_float(row.get("Units")) or 0)
        if is_void:
            result, actual, note, pnl = "VOID", None, f"{game_label} {game.get('status_name')} — refunded", 0.0
        elif pick_type == "SPREAD":
            result, actual, note = grade_spread(row, final_game)
            pnl = odds_profit(result, units, row.get("Odds"))
        elif pick_type == "TOTAL":
            result, actual, note = grade_total(row, final_game)
            pnl = odds_profit(result, units, row.get("Odds"))
        else:
            continue
        graded_at = now_iso()
        clv_row = matching_clv_row(clv_rows, ref, pick_type, row.get("Selection"), row.get("Player/Team"))
        rec = result_record_from_source(date, sport_label, row, ref, result, actual, units, pnl, graded_at, note, game_label, clv_row)
        upsert_result_row(results_ws, rec)
        graded.append({**rec, "sport": sport_label, "game": game_label, "ref": ref, "result": result, "actual": actual, "units": units, "pnl": pnl, "graded_at": graded_at, "note": note})

    prop_headers = [c.value for c in props_ws[1]]
    for vals in props_ws.iter_rows(min_row=2, values_only=True):
        if not vals or str(vals[0] or "")[:10] != date:
            continue
        row = dict(zip(prop_headers, vals))
        if not game_matches_row(game, row):
            continue
        ref = f"PROP:{row.get('Player Name')} {row.get('Stat')} {row.get('Line')}"
        if ref in already:
            continue
        units = units_from_prop_reason(row)
        if is_void:
            result, actual, note, pnl = "VOID", None, f"{game_label} {game.get('status_name')} — refunded", 0.0
        else:
            result, actual, note = grade_prop(row, player_stats, True)
            if result == "PENDING" and "No final stat line" in note:
                result = "MANUAL REVIEW"
                manual_reviews.append({"player": row.get("Player Name"), "stat": row.get("Stat"), "game": game_label, "note": note})
            pnl = odds_profit(result, units, None)
        graded_at = now_iso()
        clv_row = matching_clv_row(clv_rows, ref, "PROP", row.get("Selection"), row.get("Player Name"))
        rec = result_record_from_source(date, sport_label, row, ref, result, actual, units, pnl, graded_at, note, game_label, clv_row, {"Pick Type": "PROP", "Player/Team": row.get("Player Name"), "Pick": row.get("Selection"), "Platform": row.get("Platform") or "", "Confidence Tier": row.get("Confidence")})
        upsert_result_row(results_ws, rec)
        graded.append({**rec, "sport": sport_label, "game": game_label, "ref": ref, "result": result, "actual": actual, "units": units, "pnl": pnl, "graded_at": graded_at, "note": note})

    parlay_headers = [c.value for c in parlays_ws[1]]
    for vals in parlays_ws.iter_rows(min_row=2, values_only=True):
        if not vals or str(vals[0] or "")[:10] != date:
            continue
        row = dict(zip(parlay_headers, vals))
        if not game_matches_row(game, row):
            continue
        ref = f"PARLAY:{row.get('Parlay Name')}"
        if ref in already:
            continue
        units = float(to_float(row.get("Units")) or 0.5)
        if is_void:
            result, actual, note = "VOID", None, f"{game_label} postponed/suspended — parlay refunded"
        else:
            leg_results = [g["result"] for g in graded if g["result"] in {"WIN", "LOSS", "PUSH"}]
            if leg_results and all(x == "WIN" for x in leg_results):
                result = "WIN"
            elif any(x == "LOSS" for x in leg_results):
                result = "LOSS"
            elif any(x == "PUSH" for x in leg_results):
                result = "PUSH"
            else:
                result = "PENDING"
            actual = ", ".join(leg_results)
            note = f"Parlay legs from {game_label}: {actual or 'no same-game graded legs found'}"
        pnl = odds_profit(result, units, None)
        graded_at = now_iso()
        rec = result_record_from_source(date, sport_label, row, ref, result, actual, units, pnl, graded_at, note, game_label, {}, {"Pick Type": "PARLAY", "Pick": row.get("Legs"), "Edge Type Tags": "Correlated", "Correlation Group": row.get("Correlation Group") or row.get("Parlay Name"), "Slip ID": row.get("Slip ID")})
        upsert_result_row(results_ws, rec)
        graded.append({**rec, "sport": sport_label, "game": game_label, "ref": ref, "result": result, "actual": actual, "units": units, "pnl": pnl, "graded_at": graded_at, "note": note})

    if dry_run:
        wb.close()
        return {"sport": sport_label, "game": game_label, "would_grade": graded, "manual_reviews": manual_reviews, "workbook": str(path)}
    save_workbook_atomic(wb, path)
    summary = sync_master_and_bankroll(date, graded) if graded else {"bankroll": bankroll_state(), "daily_rows": [], "day_pnl": 0, "current": bankroll_state().get("current_bankroll", 100)}
    return {"sport": sport_label, "game": game_label, "graded": graded, "manual_reviews": manual_reviews, "workbook": str(path), **summary}


def build_game_recap(result: dict[str, Any]) -> str:
    emoji = "🏀" if result.get("sport") == "NBA" else "⚾"
    rows = result.get("graded", [])
    if not rows:
        return ""
    game = result.get("game") or "Game"
    status_icon = {"WIN": "✅", "LOSS": "❌", "PUSH": "🔄", "VOID": "⚠️", "MANUAL REVIEW": "⚠️", "PENDING": "⏳"}
    lines = [f"{emoji} GAME FINAL: {game}", "", "Results:"]
    for r in rows:
        lines.append(f"[{r['result']} {status_icon.get(r['result'], '')}] {r['ref']} — {fmt_units(r.get('pnl'))} units")
    game_pnl = round(sum(float(to_float(r.get("pnl")) or 0) for r in rows), 3)
    daily = result.get("daily_rows", [])
    wins = len([r for r in daily if r.get("result") == "WIN"])
    losses = len([r for r in daily if r.get("result") == "LOSS"])
    pushes = len([r for r in daily if r.get("result") == "PUSH"])
    lines.extend(["", f"Game PNL: {fmt_units(game_pnl)} units", f"Running Today: {fmt_units(result.get('day_pnl'))} units ({wins}-{losses}-{pushes} record so far today)", f"Bankroll: {fmt_units(result.get('current'))} units"])
    for m in result.get("manual_reviews", []):
        lines.append(f"\n⚠️ MANUAL REVIEW: {m.get('player')} {m.get('stat')} stat not found in ESPN box score for {m.get('game')}")
    return "\n".join(lines)


def game_completion_monitor(date: str | None = None, dry_run: bool = False, reconciliation: bool = False) -> dict[str, Any]:
    ensure_dirs()
    date = date or today_str()
    cache = load_game_status_cache()
    results = []
    checked_games = 0
    for sport in ["nba", "mlb"]:
        games = espn_scoreboard_games_for_date(sport, date)
        for game in games:
            event_id = str(game.get("event_id") or "")
            if not event_id:
                continue
            checked_games += 1
            state = cache.setdefault(event_id, {})
            state.update({"status": game.get("status"), "home_team": game.get("home_team"), "away_team": game.get("away_team"), "home_score": game.get("home_score"), "away_score": game.get("away_score"), "status_name": game.get("status_name")})
            should_grade = game.get("status") in {"final", "void"} and not state.get("graded")
            if reconciliation and game.get("status") in {"final", "void"}:
                should_grade = True
            if not should_grade:
                continue
            graded = grade_game_in_workbook(sport, game, date, dry_run=dry_run)
            results.append({"event_id": event_id, **graded})
            if not dry_run:
                state["graded"] = True
                if game.get("status") == "void":
                    send_telegram(f"⚠️ GAME VOID: {graded.get('game')} postponed/suspended\nAll picks refunded. Bankroll restored.")
                else:
                    recap = build_game_recap(graded)
                    if recap:
                        send_telegram(recap)
                    for m in graded.get("manual_reviews", []):
                        send_telegram(f"⚠️ MANUAL REVIEW: {m.get('player')} {m.get('stat')} not found in ESPN box score for {m.get('game')}")
    if not dry_run:
        save_game_status_cache(cache)
    log(f"game_completion_monitor checked_games={checked_games} graded_games={len(results)} reconciliation={reconciliation}")
    return {"status": "ok", "task": "game_completion_monitor", "date": date, "checked_games": checked_games, "graded_games": len(results), "games": results}


def build_daily_recap(summary: dict[str, Any], date: str) -> str:
    rows = summary.get("daily_rows", [])
    def filt(sport=None, typ=None):
        out = rows
        if sport:
            out = [r for r in out if r.get("sport") == sport]
        if typ:
            out = [r for r in out if typ in str(r.get("ref") or "")]
        return out
    def rec(rs):
        return f"{len([r for r in rs if r.get('result')=='WIN'])}-{len([r for r in rs if r.get('result')=='LOSS'])}-{len([r for r in rs if r.get('result')=='PUSH'])}"
    def pnl(rs):
        return fmt_units(sum(float(to_float(r.get("pnl")) or 0) for r in rs))
    graded = [r for r in rows if r.get("result") not in {"PENDING", "MANUAL REVIEW"}]
    best = max(graded, key=lambda r: float(to_float(r.get("pnl")) or 0), default={})
    worst = min(graded, key=lambda r: float(to_float(r.get("pnl")) or 0), default={})
    bankroll = summary.get("bankroll", {})
    # Build per-platform breakdown from graded rows when platform info is present.
    platform_section = ""
    platforms: dict[str, list[dict[str, Any]]] = {}
    for r in graded:
        plat = str(r.get("platform") or r.get("Platform") or "").strip()
        if plat:
            platforms.setdefault(plat, []).append(r)
    if platforms:
        parts = [f"{plat}: {rec(prows)} | {pnl(prows)} units" for plat, prows in sorted(platforms.items())]
        platform_section = "\nBy Platform:\n" + "\n".join(parts) + "\n"
    return f"""📊 ALL GAMES FINAL — {date}

NBA: {rec(filt('NBA'))} | {pnl(filt('NBA'))} units
MLB: {rec(filt('MLB'))} | {pnl(filt('MLB'))} units
{platform_section}
Day PNL: {fmt_units(summary.get('day_pnl'))} units
Bankroll: {fmt_units(summary.get('current'))}/100 units ({fmt_units(float(to_float(summary.get('current')) or 0) - 100)} from start)
ROI: {fmt_units(summary.get('roi') or bankroll.get('roi_percentage_current'))}%

By Tier:
A: n/a | B: n/a | C: n/a

By Type:
Props: {rec(filt(typ='PROP'))} | Spreads: {rec(filt(typ='SPREAD'))} | Totals: {rec(filt(typ='TOTAL'))}

Best Pick: {best.get('ref', 'N/A')} +{fmt_units(best.get('pnl', 0))} units
Worst Pick: {worst.get('ref', 'N/A')} {fmt_units(worst.get('pnl', 0))} units"""


def check_results() -> dict[str, Any]:
    """1am reconciliation: catch missed finals/pending rows, then send daily recap."""
    date = today_str()
    monitor = game_completion_monitor(date=date, reconciliation=True)
    summary = sync_master_and_bankroll(date, [])
    pending = [r for r in summary.get("daily_rows", []) if r.get("result") in {"PENDING", "MANUAL REVIEW"}]
    all_final_or_review = len(pending) == 0
    # Build per-platform breakdown from graded flat rows for recap and downstream consumers.
    platform_breakdown: dict[str, dict[str, Any]] = {}
    for r in summary.get("daily_rows", []):
        plat = str(r.get("platform") or r.get("Platform") or "").strip()
        if not plat or r.get("result") in {"PENDING", "MANUAL REVIEW"}:
            continue
        if plat not in platform_breakdown:
            platform_breakdown[plat] = {"wins": 0, "losses": 0, "pushes": 0, "pnl": 0.0}
        entry = platform_breakdown[plat]
        res = str(r.get("result") or "").upper()
        if res == "WIN":
            entry["wins"] += 1
        elif res == "LOSS":
            entry["losses"] += 1
        elif res == "PUSH":
            entry["pushes"] += 1
        entry["pnl"] = round(entry["pnl"] + float(to_float(r.get("pnl")) or 0), 3)
    for plat, stats in platform_breakdown.items():
        stats["record"] = f"{stats['wins']}-{stats['losses']}-{stats['pushes']}"
    recap = build_daily_recap(summary, date)
    send_telegram(recap if all_final_or_review else recap + f"\n\nPending/manual review rows still open: {len(pending)}")
    log(f"check_results reconciliation complete: graded_games={monitor.get('graded_games')} pending={len(pending)} day_pnl={summary.get('day_pnl')} bankroll={summary.get('current')}")
    return {"status": "ok", "task": "check_results", "mode": "reconciliation", "monitor": monitor, "pending": len(pending), "daily_recap": recap, "platform_breakdown": platform_breakdown, **summary}


