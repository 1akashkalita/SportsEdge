def load_projection_index(sport: str, date: str) -> dict[tuple[Any, ...], dict[str, Any]]:
    path = PROJECTION_DIR / sport.lower() / f"{sport}_projections_{date}.json"
    idx: dict[tuple[Any, ...], dict[str, Any]] = {}
    if not path.exists():
        return idx
    try:
        doc = json.loads(path.read_text())
    except Exception:
        return idx
    for row in doc.get("projections") or []:
        key2 = (normalize_player_name(row.get("player_name")), normalize_prop_stat(row.get("stat_type")))
        platform = str(row.get("platform") or "").strip().lower()
        line = to_float(row.get("line"))
        if line is not None:
            if platform:
                idx[(platform, key2[0], key2[1], round(float(line), 3))] = row
            idx[(key2[0], key2[1], round(float(line), 3))] = row
        # Keep a fallback for legacy callers; prefer the first projection for a player/stat
        # so different-platform/line rows do not constantly overwrite each other.
        idx.setdefault(key2, row)
    return idx


def model_score_from_tier(tier: str) -> int:
    return {"A": 3, "B": 2, "C": 1, "SKIP": 0}.get(str(tier or "").upper(), 0)


def apply_projection_adjustment(score: int, factors: list[str], projection: dict[str, Any] | None) -> int | None:
    if not projection:
        factors.append("projection model unavailable")
        return score
    tier = str(projection.get("confidence_tier") or "").upper()
    factors.append(str(projection.get("reasoning") or "projection model available"))
    for flag in projection.get("flags") or []:
        if flag not in factors:
            factors.append(str(flag))
    if tier == "SKIP":
        factors.append("projection model tier SKIP: no generated prop pick")
        return None
    current_tier = confidence_from_score(score)
    order = {"C": 1, "B": 2, "A": 3}
    if tier in order and abs(order[tier] - order.get(current_tier, 1)) > 1:
        factors.append(f"model tier {tier} overrides heuristic confidence {current_tier}")
        return model_score_from_tier(tier)
    return score


def apply_hit_rate_adjustment(score: int, factors: list[str], hit_row: dict[str, Any] | None) -> int:
    if not hit_row:
        factors.append("hit-rate data unavailable")
        return score
    hr10 = float(hit_row.get("hit_rate_l10") or 0)
    sample = int(hit_row.get("sample_size") or 0)
    if hr10 >= 0.70:
        score += 1
        factors.append(f"hit_rate_l10 {hr10:.3f}: confidence upgrade")
    elif hr10 <= 0.35:
        score -= 1
        factors.append(f"hit_rate_l10 {hr10:.3f}: confidence downgrade")
    if float(hit_row.get("vs_opponent_hit_rate") or 0) <= 0.30:
        factors.append("BAD MATCHUP")
    if hit_row.get("minutes_trend") == "down":
        factors.append("MINUTES RISK")
    if sample < 5:
        factors.append("SMALL SAMPLE")
    factors.append(f"hit rates: L5={hit_row.get('hit_rate_l5')} L10={hit_row.get('hit_rate_l10')} avgL5={hit_row.get('avg_stat_l5')} avgL10={hit_row.get('avg_stat_l10')} sample={sample}")
    return max(score, 1)


