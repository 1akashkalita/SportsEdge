def obsidian_sync(payload: dict[str, Any]) -> dict[str, Any]:
    """Route SportsEdge Obsidian writes through the canonical obsidian_sync script."""
    if not OBSIDIAN_SYNC_SCRIPT.exists():
        raise FileNotFoundError(f"obsidian_sync.py not found: {OBSIDIAN_SYNC_SCRIPT}")
    trigger = str(payload.get("trigger") or "")
    payload_json = json.dumps(payload)
    proc = subprocess.run(
        ["/usr/local/bin/python3", str(OBSIDIAN_SYNC_SCRIPT), "--trigger", trigger, "--payload", payload_json],
        text=True,
        capture_output=True,
        timeout=60,
    )
    raw = proc.stdout.strip() or proc.stderr.strip()
    try:
        result = json.loads(raw) if raw else {}
    except Exception:
        result = {"success": False, "writes": [], "errors": [raw or f"exit {proc.returncode}"]}
    if proc.returncode != 0 or not result.get("success", False):
        raise RuntimeError(f"obsidian_sync failed for {payload.get('trigger')}: {result}")
    return result

