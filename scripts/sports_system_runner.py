def detect_prop_market_opportunities(
    sport: str,
    sportsbook_lines: dict[tuple[str, str], dict[str, list[dict[str, Any]]]],
    prizepicks_props: list[dict[str, Any]],
) -> tuple[list[dict[str, Any]], list[dict[str, Any]]]:
    """Detect middle and true price-arb candidates from sportsbook and PrizePicks prop lines."""
    middle_opportunities: list[dict[str, Any]] = []
    arb_opportunities: list[dict[str, Any]] = []
    seen_middle: set[tuple[Any, ...]] = set()
    seen_arb: set[tuple[Any, ...]] = set()

    pp_lookup = {
        (normalize_player_name(p.get("player_name")), normalize_prop_stat(p.get("stat_name") or p.get("stat_type"))): p
        for p in prizepicks_props
    }
    for key, sides in sportsbook_lines.items():
        player_key, stat = key
        overs = sides.get("over", [])
        unders = sides.get("under", [])
        if not overs or not unders:
            continue
        low_over = min(overs, key=lambda x: x["line"])
        high_under = max(unders, key=lambda x: x["line"])
        if high_under["line"] - low_over["line"] >= 0.5:
            signature = (player_key, stat, low_over["book"], low_over["line"], high_under["book"], high_under["line"])
            if signature not in seen_middle:
                seen_middle.add(signature)
                middle_opportunities.append({
                    "player": player_key.title(), "stat": stat, "low_over_line": low_over["line"], "low_over_book": low_over["book"],
                    "high_under_line": high_under["line"], "high_under_book": high_under["book"],
                    "middle_width": high_under["line"] - low_over["line"],
                    "action": "watch" if high_under["line"] - low_over["line"] < 1.0 else "active",
                })
        # Price arbitrage only when the best over and under are at the same point and implied probabilities sum below 1.
        for line in sorted({o["line"] for o in overs} & {u["line"] for u in unders}):
            over_candidates = [o for o in overs if o["line"] == line]
            under_candidates = [u for u in unders if u["line"] == line]
            best_over = min(over_candidates, key=lambda x: american_implied_prob(x.get("price")) or 9)
            best_under = min(under_candidates, key=lambda x: american_implied_prob(x.get("price")) or 9)
            over_prob = american_implied_prob(best_over.get("price"))
            under_prob = american_implied_prob(best_under.get("price"))
            if over_prob is not None and under_prob is not None and over_prob + under_prob < 1.0:
                signature = (player_key, stat, line, best_over["book"], best_under["book"])
                if signature not in seen_arb:
                    seen_arb.add(signature)
                    arb_opportunities.append({
                        "player": player_key.title(), "stat": stat, "line": line,
                        "over_book": best_over["book"], "over_price": best_over.get("price"),
                        "under_book": best_under["book"], "under_price": best_under.get("price"),
                        "hold": round(over_prob + under_prob, 4), "action": "active",
                    })

    # PrizePicks-vs-sportsbook middles: PP line creates a middle with best sportsbook over/under.
    for key, p in pp_lookup.items():
        pp_line = to_float(p.get("line_score"))
        if pp_line is None or key not in sportsbook_lines:
            continue
        sides = sportsbook_lines[key]
        for over in sides.get("over", []):
            if pp_line - over["line"] >= 0.5:
                middle_opportunities.append({
                    "player": p.get("player_name") or key[0].title(), "stat": p.get("stat_name") or p.get("stat_type") or key[1],
                    "low_over_line": over["line"], "low_over_book": over["book"],
                    "high_under_line": pp_line, "high_under_book": "PrizePicks",
                    "middle_width": pp_line - over["line"],
                    "action": "watch" if pp_line - over["line"] < 1.0 else "active",
                })
        for under in sides.get("under", []):
            if under["line"] - pp_line >= 0.5:
                middle_opportunities.append({
                    "player": p.get("player_name") or key[0].title(), "stat": p.get("stat_name") or p.get("stat_type") or key[1],
                    "low_over_line": pp_line, "low_over_book": "PrizePicks",
                    "high_under_line": under["line"], "high_under_book": under["book"],
                    "middle_width": under["line"] - pp_line,
                    "action": "watch" if under["line"] - pp_line < 1.0 else "active",
                })
    return middle_opportunities[:25], arb_opportunities[:25]


def prop_monitor_prop_id(p: dict[str, Any]) -> Any:
    return p.get("projection_id") or f"{p.get('player_name')}:{p.get('stat_name') or p.get('stat_type')}:{p.get('line_score')}"


def prop_monitor_refresh_rows(active_full: list[dict[str, Any]], special_limit: int | None = None) -> tuple[list[dict[str, Any]], dict[str, Any]]:
    """Return workbook-display rows without using that subset for board monitoring logic."""
    if special_limit is None:
        special_limit = int(os.environ.get("PROP_MONITOR_SPECIAL_ROWS_LIMIT", "300"))
    standard_rows = [p for p in active_full if str(p.get("odds_type") or "").lower() == "standard"]
    demon_rows = [p for p in active_full if str(p.get("odds_type") or "").lower() == "demon"]
    goblin_rows = [p for p in active_full if str(p.get("odds_type") or "").lower() == "goblin"]
    other_rows = [p for p in active_full if str(p.get("odds_type") or "").lower() not in {"standard", "demon", "goblin"}]
    special_rows = (demon_rows + goblin_rows + other_rows)[:max(special_limit, 0)]
    refresh_rows = standard_rows + special_rows
    return refresh_rows, {
        "workbook_rows_refreshed_limit": f"all_standard_plus_{max(special_limit, 0)}_special_rows",
        "standard_rows_refreshed": len(standard_rows),
        "demon_rows_refreshed": sum(1 for p in special_rows if str(p.get("odds_type") or "").lower() == "demon"),
        "goblin_rows_refreshed": sum(1 for p in special_rows if str(p.get("odds_type") or "").lower() == "goblin"),
        "other_rows_refreshed": sum(1 for p in special_rows if str(p.get("odds_type") or "").lower() not in {"demon", "goblin"}),
    }


