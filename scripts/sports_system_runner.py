def obsidian_update_bankroll_files(date: str, bankroll: dict[str, Any], all_graded: list[dict[str, Any]], current: Any, roi: Any, day_pnl: Any) -> None:
    bankroll_md = OBSIDIAN_DASHBOARD / "Bankroll.md"
    wins = len([r for r in all_graded if r.get("result") == "WIN"])
    losses = len([r for r in all_graded if r.get("result") == "LOSS"])
    pushes = len([r for r in all_graded if r.get("result") == "PUSH"])
    units = round(sum(float(r.get("units") or 0) for r in all_graded if r.get("result") != "PENDING"), 3)
    row = f"| {date} | Daily grading | {wins}-{losses}-{pushes} | {fmt_units(units)} | {fmt_units(day_pnl)} | {fmt_units(current)} |"
    text = bankroll_md.read_text() if bankroll_md.exists() else ""
    filtered = [ln for ln in text.splitlines() if not ln.startswith(f"| {date} | Daily grading |")]
    filtered.append(row)
    bankroll_markdown = "\n".join(filtered).rstrip() + "\n"
    home = OBSIDIAN_DASHBOARD / "Home.md"
    home_markdown = None
    if home.exists():
        total_profit = bankroll.get("overall_profit_loss", 0)
        record = f"{wins}-{losses}-{pushes}"
        txt = home.read_text()
        replacements = {
            "| Starting Bankroll |": f"| Starting Bankroll | {fmt_units(bankroll.get('starting_bankroll', 100))} units |",
            "| Current Bankroll  |": f"| Current Bankroll  | {fmt_units(current)} units |",
            "| Overall PNL       |": f"| Overall PNL       | {fmt_units(total_profit)} units |",
            "| Overall ROI       |": f"| Overall ROI       | {fmt_units(roi)}%        |",
            "| Record            |": f"| Record            | {record}     |",
        }
        out=[]
        for ln in txt.splitlines():
            for prefix, repl in replacements.items():
                if ln.startswith(prefix):
                    ln = repl
                    break
            if ln.startswith("updated:"):
                ln = f"updated: {date}"
            out.append(ln)
        home_markdown = "\n".join(out).rstrip() + "\n"
    payload_data = {"bankroll_markdown": bankroll_markdown, "skip_home_write": home_markdown is None}
    if home_markdown is not None:
        payload_data["home_markdown"] = home_markdown
    obsidian_sync({"trigger": "game_completion_monitor", "sport": "NBA", "date": date, "data": payload_data})


def obsidian_update_player_research(sport: str, picks: list[dict[str, Any]], date: str | None = None) -> None:
    # Create/update player notes when today's generated data already shows 3+ A-tier player props for the same player.
    date = date or today_str()
    counts: dict[str, list[dict[str, Any]]] = {}
    for r in picks:
        if not str(r.get("Confidence") or "").upper().startswith("A"):
            continue
        if str(r.get("Pick Type") or "").upper() != "PROP":
            continue
        sel = str(r.get("Selection") or "")
        player = sel.split(" Over ")[0].split(" Under ")[0].strip()
        if player:
            counts.setdefault(player, []).append(r)
    notes = []
    for player, rows in counts.items():
        if len(rows) < 3:
            continue
        safe = "".join(ch for ch in player if ch.isalnum() or ch in " -_.").strip().replace("/", "-")
        path = OBSIDIAN_RESEARCH_PLAYERS / f"{safe}.md"
        team = rows[0].get("Away Team") or rows[0].get("Home Team") or ""
        content = f"""---
player: {player}
team: {team}
position:
last_updated: {date}
---

# {player}

## PrizePicks Hit Rate

| Stat | Line | Result | Date |
| ---- | ---- | ------ | ---- |

## Notes

* Appeared in {len(rows)} A-tier {sport.upper()} picks during the week of {date}.

## Injury History This Season

*

## Best Platform for Props

*

---
"""
        if path.exists():
            txt = path.read_text()
            if "## Notes" in txt:
                content = txt.replace("## Notes\n\n*", f"## Notes\n\n* Appeared in {len(rows)} A-tier {sport.upper()} picks during the week of {date}.\n*", 1)
            else:
                content = txt
        notes.append({"player": player, "markdown": content})
    if notes:
        obsidian_sync({"trigger": "check_results", "sport": sport.upper(), "date": date, "data": {"player_notes": notes, "skip_standard_check_results": True}})


