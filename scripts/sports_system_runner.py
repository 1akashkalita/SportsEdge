def clear_generated_rows(ws, date: str, generated_marker: str = "Generated by sports_system_runner") -> int:
    removed = 0
    for r in range(ws.max_row, 1, -1):
        row_date = str(ws.cell(r, 1).value or "")[:10]
        row_values = [str(ws.cell(r, c).value or "") for c in range(1, ws.max_column + 1)]
        if row_date == date and (
            any(generated_marker in value for value in row_values)
            or any("No wager auto-generated" in value for value in row_values)
            or any("Schedule baseline created by sports_system_runner" in value for value in row_values)
        ):
            ws.delete_rows(r, 1)
            removed += 1
    return removed


def clear_today_rows(ws, date: str) -> int:
    removed = 0
    for r in range(ws.max_row, 1, -1):
        if str(ws.cell(r, 1).value or "")[:10] == date:
            ws.delete_rows(r, 1)
            removed += 1
    return removed


def _header_index(ws) -> dict[str, int]:
    return {str(ws.cell(1, c).value or "").strip(): c for c in range(1, ws.max_column + 1)}


def _cell_by_header(ws, row_num: int, headers: dict[str, int], name: str, default: Any = None) -> Any:
    col = headers.get(name)
    return ws.cell(row_num, col).value if col else default


def _row_contains_marker(ws, row_num: int, marker: str = GENERATED_MARKER) -> bool:
    return any(marker in str(ws.cell(row_num, c).value or "") for c in range(1, ws.max_column + 1))


def global_daily_exposure(date: str | None = None) -> dict[str, Any]:
    """Return active units at risk today across NBA + MLB without double-counting Props sheet mirrors."""
    date = date or today_str()
    total = 0.0
    rows: list[dict[str, Any]] = []
    for sport in ["nba", "mlb"]:
        path = workbook_path(sport, date)
        if not path.exists() or not workbook_is_valid(path):
            continue
        wb = safe_load_workbook(path, read_only=True, data_only=True)
        try:
            if "Picks" in wb.sheetnames:
                ws = wb["Picks"]
                headers = _header_index(ws)
                for r in range(2, ws.max_row + 1):
                    if str(_cell_by_header(ws, r, headers, "Date", "") or "")[:10] != date:
                        continue
                    status = str(_cell_by_header(ws, r, headers, "Status", "") or "").upper()
                    reason = str(_cell_by_header(ws, r, headers, "Reasoning", "") or "").upper()
                    if not ("APPROVED" in status or "APPROVED" in reason or status == "ACTIVE"):
                        continue
                    units = to_float(_cell_by_header(ws, r, headers, "Units", 0)) or 0.0
                    if units <= 0:
                        continue
                    total += units
                    rows.append({
                        "sport": sport.upper(), "sheet": "Picks", "row": r, "units": units,
                        "selection": _cell_by_header(ws, r, headers, "Selection", ""),
                        "platform": _cell_by_header(ws, r, headers, "Platform", ""),
                    })
            if "Correlated Parlays" in wb.sheetnames:
                ws = wb["Correlated Parlays"]
                headers = _header_index(ws)
                for r in range(2, ws.max_row + 1):
                    if str(_cell_by_header(ws, r, headers, "Date", "") or "")[:10] != date:
                        continue
                    status = str(_cell_by_header(ws, r, headers, "Status", "") or "").upper()
                    if status != "ACTIVE":
                        continue
                    units = to_float(_cell_by_header(ws, r, headers, "Units", 0)) or 0.0
                    if units <= 0:
                        continue
                    total += units
                    rows.append({
                        "sport": sport.upper(), "sheet": "Correlated Parlays", "row": r, "units": units,
                        "selection": _cell_by_header(ws, r, headers, "Parlay Name", ""),
                        "platform": "Parlay",
                    })
        finally:
            wb.close()
    return {"date": date, "exposure": round(total, 3), "rows": rows}


