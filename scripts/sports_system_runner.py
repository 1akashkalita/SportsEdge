def selection_side_for_clv(selection: Any, pick_type: Any = None) -> str:
    s = str(selection or "").strip()
    low = s.lower()
    if low.startswith("under") or " under " in f" {low} ":
        return "Under"
    if low.startswith("over") or " over " in f" {low} ":
        return "Over"
    return "Spread" if str(pick_type or "").upper() == "SPREAD" else "Over"


def clv_pick_id(sport: str, pick: dict[str, Any]) -> str:
    return safe_key(
        sport.upper(),
        pick.get("date") or today_str(),
        pick.get("kind") or pick.get("Pick Type"),
        pick.get("game_id") or pick.get("Game ID") or pick.get("projection_id"),
        pick.get("selection") or pick.get("Selection"),
    )


def record_morning_clv_row(ws, sport: str, pick: dict[str, Any]) -> bool:
    """Store opening/morning line for a generated pick exactly when daily picks writes it."""
    headers = clv_headers(ws)
    date = pick.get("date") or today_str()
    pick_type = str(pick.get("kind") or pick.get("Pick Type") or "pick").upper()
    selection = pick.get("selection") or pick.get("Selection") or ""
    line = to_float(pick.get("line") if "line" in pick else pick.get("Line"))
    if line is None:
        return False
    pid = pick.get("key") or clv_pick_id(sport, pick)
    # Idempotent: one CLV row per generated pick id/day.
    existing = None
    for r in range(2, ws.max_row + 1):
        if str(ws.cell(r, headers["Pick ID"]).value or "") == str(pid):
            existing = r
            break
    if existing is None:
        ws.append([None] * len(CLV_EXTENDED_HEADERS))
        existing = ws.max_row
    player_game = pick.get("player") or f"{pick.get('away_team') or ''} @ {pick.get('home_team') or ''}".strip(" @") or selection
    values = {
        "Date": date,
        "Sport": sport.upper(),
        "Platform": pick.get("platform") or pick.get("Platform") or ("PrizePicks" if pick_type == "PROP" else ""),
        "Pick ID": pid,
        "Game ID": pick.get("game_id") or pick.get("Game ID"),
        "Pick Ref": selection,
        "Pick Type": pick_type,
        "Selection": selection,
        "Side": selection_side_for_clv(selection, pick_type),
        "Player/Game": player_game,
        "Our Line": line,
        "Morning Line": pick.get("best_available_line") if pick.get("kind") in {"spread", "total"} and pick.get("best_available_line") not in (None, "") else line,
        "Morning Timestamp": now_iso(),
        "Status": "MORNING LINE STORED",
        "Confidence": pick.get("confidence") or pick.get("Confidence"),
        "Notes": "CLV baseline from FanDuel/DraftKings best available game-market line" if pick.get("clv_baseline_book") else "Opening/morning line captured by daily_picks",
    }
    for h, v in values.items():
        ws.cell(existing, headers[h]).value = v
    return True


def match_game_for_clv_pick(row: dict[str, Any], games: list[dict[str, Any]]) -> dict[str, Any] | None:
    gid = str(row.get("Game ID") or "")
    if gid:
        for g in games:
            if gid == str(g.get("id") or g.get("event_id") or ""):
                return g
    ref = str(row.get("Pick Ref") or row.get("Selection") or "")
    player_game = str(row.get("Player/Game") or "")
    haystack = f"{ref} {player_game}".lower()
    for g in games:
        home = str(g.get("home_team") or "")
        away = str(g.get("away_team") or "")
        if (home and home.lower() in haystack) or (away and away.lower() in haystack):
            return g
    return None


def current_game_line_for_pick(row: dict[str, Any], games: list[dict[str, Any]]) -> float | None:
    game = match_game_for_clv_pick(row, games)
    if not game:
        return None
    pick_type = str(row.get("Pick Type") or "").upper()
    selection = str(row.get("Selection") or row.get("Pick Ref") or "")
    if pick_type == "TOTAL":
        market = game_market(game, "totals")
        if not market:
            return None
        side = selection_side_for_clv(selection, pick_type)
        for outcome in market.get("outcomes", []) or []:
            if str(outcome.get("name") or "").lower() == side.lower():
                return to_float(outcome.get("point"))
        pts = [to_float(o.get("point")) for o in market.get("outcomes", []) or []]
        pts = [x for x in pts if x is not None]
        return pts[0] if pts else None
    if pick_type == "SPREAD":
        market = game_market(game, "spreads")
        if not market:
            return None
        for outcome in market.get("outcomes", []) or []:
            name = str(outcome.get("name") or "")
            if name and (name.lower() in selection.lower() or selection.lower().startswith(name.lower())):
                return to_float(outcome.get("point"))
        pts = [to_float(o.get("point")) for o in market.get("outcomes", []) or []]
        pts = [x for x in pts if x is not None]
        return pts[0] if pts else None
    return None


def current_prop_line_for_pick(row: dict[str, Any], pp: list[dict[str, Any]]) -> float | None:
    player_game = str(row.get("Player/Game") or "")
    selection = str(row.get("Selection") or row.get("Pick Ref") or "")

    def norm_stat(s: str) -> str:
        return "".join(ch for ch in s.lower().replace("rebounds", "rebs").replace("assists", "asts") if ch.isalnum())

    stat = ""
    # Longest stat tokens first so Pts+Rebs+Asts wins over Pts+Rebs.
    for token in ["Pts+Rebs+Asts", "Pts+Rebs", "Pts+Asts", "Rebs+Asts", "Blks+Stls", "Total Bases", "Home Runs", "Strikeouts", "Points", "Rebounds", "Assists", "Hits", "Runs", "RBIs"]:
        if token.lower() in selection.lower():
            stat = norm_stat(token)
            break
    exact: list[dict[str, Any]] = []
    player_only: list[dict[str, Any]] = []
    for p in pp:
        pname = str(p.get("player_name") or "")
        if not pname or not (pname.lower() in player_game.lower() or pname.lower() in selection.lower()):
            continue
        pstat = norm_stat(str(p.get("stat_name") or p.get("stat_type") or ""))
        if stat and pstat == stat:
            exact.append(p)
        player_only.append(p)
    candidates = exact or ([] if stat else player_only)
    if not candidates:
        return None
    # Prefer standard line if multiple matching variants exist.
    candidates.sort(key=lambda p: 0 if str(p.get("odds_type") or "").lower() == "standard" else 1)
    return to_float(candidates[0].get("line_score"))


def calculate_clv_value(side: str, our_line: float, closing_line: float) -> float:
    side_l = str(side or "").lower()
    if side_l == "under":
        return round(closing_line - our_line, 3)
    # Per requested formula: overs use our_line - closing_line; spreads also use our_line - closing_line.
    return round(our_line - closing_line, 3)


def clv_status(value: float | None) -> str:
    if value is None:
        return "PENDING"
    if value > 0:
        return "POSITIVE CLV"
    if value < 0:
        return "NEGATIVE CLV"
    return "NEUTRAL"


def build_weekly_clv_report(sport: str, summary: dict[str, Any]) -> str:
    by_conf = summary.get("by_confidence", {})
    by_type = summary.get("by_type", {})
    def fmt_map(d: dict[str, Any]) -> str:
        return ", ".join(f"{k}: {fmt_units(v)}" for k, v in sorted(d.items())) or "None"
    return (
        f"📈 WEEKLY CLV REPORT — {sport.upper()}\n"
        f"Week ending: {today_str()}\n"
        f"Tracked picks: {summary.get('graded_count', 0)}\n"
        f"Average CLV: {fmt_units(summary.get('average_clv'))}\n"
        f"Positive/Negative/Neutral: {summary.get('positive', 0)}/{summary.get('negative', 0)}/{summary.get('neutral', 0)}\n"
        f"By confidence: {fmt_map(by_conf)}\n"
        f"By pick type: {fmt_map(by_type)}"
    )


def weekly_clv_summary(ws, sport: str) -> dict[str, Any]:
    headers = clv_headers(ws)
    today = datetime.now().date()
    start = today - timedelta(days=6)
    vals: list[tuple[float, str, str]] = []
    positive = negative = neutral = 0
    for r in range(2, ws.max_row + 1):
        if str(ws.cell(r, headers["Sport"]).value or "").upper() != sport.upper():
            continue
        try:
            d = datetime.fromisoformat(str(ws.cell(r, headers["Date"]).value)[:10]).date()
        except Exception:
            continue
        if not (start <= d <= today):
            continue
        clv = to_float(ws.cell(r, headers["CLV"]).value)
        if clv is None:
            continue
        conf = str(ws.cell(r, headers["Confidence"]).value or "Unknown")
        ptype = str(ws.cell(r, headers["Pick Type"]).value or "Unknown")
        vals.append((clv, conf, ptype))
        if clv > 0: positive += 1
        elif clv < 0: negative += 1
        else: neutral += 1
    by_conf: dict[str, list[float]] = {}
    by_type: dict[str, list[float]] = {}
    for val, conf, ptype in vals:
        by_conf.setdefault(conf, []).append(val)
        by_type.setdefault(ptype, []).append(val)
    return {
        "graded_count": len(vals),
        "average_clv": round(sum(v[0] for v in vals) / len(vals), 3) if vals else 0,
        "positive": positive,
        "negative": negative,
        "neutral": neutral,
        "by_confidence": {k: round(sum(v)/len(v), 3) for k, v in by_conf.items()},
        "by_type": {k: round(sum(v)/len(v), 3) for k, v in by_type.items()},
    }


def clv_tracker(sport: str) -> dict[str, Any]:
    ensure_dirs()
    games, headers = odds_api(sport)
    if games is None:
        games = []
        headers = {"credits_remaining": "UNAVAILABLE", "credits_used": "UNAVAILABLE", "status_code": "UNAVAILABLE"}
        log("Odds API unavailable — CLV tracker continuing with DFS closing data only")
    pp: list[dict[str, Any]] = []
    try:
        # Closing lines for generated prop picks come from latest first-class DFS snapshots.
        run_fetch_dfs_props(sport)
        pp = first_class_dfs_props_latest(sport)
    except Exception as e:
        log(f"{sport.upper()} CLV prop closing line refresh skipped: {e}")
    path = ensure_workbook(sport)
    wb = safe_load_workbook(path)
    clv = wb["CLV Tracker"]
    headers_map = clv_headers(clv)
    picks = wb["Picks"] if "Picks" in wb.sheetnames else None
    date = today_str()

    # Backfill morning CLV rows from today's Picks in case the workbook predates this update.
    backfilled = 0
    if picks is not None:
        pick_headers = [c.value for c in picks[1]]
        for vals in picks.iter_rows(min_row=2, values_only=True):
            if not vals or str(vals[0])[:10] != date:
                continue
            row = dict(zip(pick_headers, vals))
            if str(row.get("Status") or "").upper().startswith("SKIP"):
                continue
            p = {
                "date": date,
                "sport": sport.upper(),
                "kind": str(row.get("Pick Type") or "pick").lower(),
                "game_id": row.get("Game ID"),
                "selection": row.get("Selection"),
                "line": row.get("Line"),
                "confidence": row.get("Confidence"),
                "away_team": row.get("Away Team"),
                "home_team": row.get("Home Team"),
                "key": clv_pick_id(sport, row),
            }
            before = clv.max_row
            if record_morning_clv_row(clv, sport, p) and clv.max_row > before:
                backfilled += 1

    updated = pending = positive = negative = neutral = 0
    preview: list[dict[str, Any]] = []
    for r in range(2, clv.max_row + 1):
        if str(clv.cell(r, headers_map["Date"]).value or "")[:10] != date:
            continue
        if str(clv.cell(r, headers_map["Sport"]).value or "").upper() != sport.upper():
            continue
        row = {h: clv.cell(r, c).value for h, c in headers_map.items()}
        if not row.get("Pick ID") or not row.get("Pick Type"):
            # Ignore legacy placeholder rows created before real pick-level CLV tracking.
            continue
        our = to_float(row.get("Our Line") if row.get("Our Line") not in (None, "") else row.get("Morning Line"))
        if our is None:
            pending += 1
            continue
        ptype = str(row.get("Pick Type") or "").upper()
        closing = current_prop_line_for_pick(row, pp) if ptype == "PROP" else current_game_line_for_pick(row, games)
        if closing is None:
            clv.cell(r, headers_map["Status"]).value = "PENDING CLOSING LINE"
            clv.cell(r, headers_map["Notes"]).value = "No current closing line match from Odds API/DFS board"
            pending += 1
            continue
        val = calculate_clv_value(str(row.get("Side") or selection_side_for_clv(row.get("Selection"), ptype)), our, closing)
        status = clv_status(val)
        clv.cell(r, headers_map["Closing Line"]).value = closing
        clv.cell(r, headers_map["Closing Timestamp"]).value = now_iso()
        clv.cell(r, headers_map["CLV"]).value = val
        clv.cell(r, headers_map["Status"]).value = status
        clv.cell(r, headers_map["Notes"]).value = "Closing/current line captured by clv_tracker"
        updated += 1
        if status == "POSITIVE CLV": positive += 1
        elif status == "NEGATIVE CLV": negative += 1
        else: neutral += 1
        if len(preview) < 10:
            preview.append(clv_preview_row(row, our, closing, val, status))

    summary = weekly_clv_summary(clv, sport)
    master_clv_rows_updated = sync_master_clv_fields(date, sport.upper(), clv_lookup_from_sheet(clv, date, sport.upper()))
    telegram_sent = False
    weekly_marker = f"WEEKLY_CLV_REPORT_SENT {sport.upper()} {date}"
    already_sent = RUN_LOG.exists() and weekly_marker in RUN_LOG.read_text(errors="ignore")
    if datetime.now().weekday() == 0 and not already_sent:
        telegram_sent = send_telegram(build_weekly_clv_report(sport, summary))
        if telegram_sent:
            log(weekly_marker)
        obsidian_create_weekly_recap(date, summary)
    save_workbook_atomic(wb, path)
    log(
        f"{sport.upper()} CLV tracker complete: rows_updated={updated} pending={pending} "
        f"positive={positive} negative={negative} neutral={neutral} backfilled={backfilled} "
        f"master_clv_rows_updated={master_clv_rows_updated} "
        f"weekly_report_sent={telegram_sent} workbook={path} credits_remaining={headers['credits_remaining']}"
    )
    return {
        "status": "ok",
        "task": f"{sport}_clv_tracker",
        "rows_updated": updated,
        "pending": pending,
        "positive": positive,
        "negative": negative,
        "neutral": neutral,
        "backfilled_morning_rows": backfilled,
        "master_clv_rows_updated": master_clv_rows_updated,
        "weekly_summary": summary,
        "weekly_report_sent": telegram_sent,
        "workbook": str(path),
        "credits_remaining": headers["credits_remaining"],
        "clv_preview": preview,
    }


def task_workbook_paths(task: str) -> list[Path]:
    """Workbook files that a task may mutate; used for cooperative lock files."""
    date = today_str()
    sport = None
    if task.startswith("nba_"):
        sport = "nba"
    elif task.startswith("mlb_"):
        sport = "mlb"
    if task in {"nba_daily_picks", "nba_prop_monitor", "nba_injury_monitor", "nba_clv_tracker"}:
        return [workbook_path("nba", date)]
    if task in {"mlb_daily_picks", "mlb_prop_monitor", "mlb_injury_monitor", "mlb_clv_tracker"}:
        return [workbook_path("mlb", date)]
    if task in {"game_completion_monitor", "check_results"}:
        return [workbook_path("nba", date), workbook_path("mlb", date), PNL_DIR / "master_pnl.xlsx"]
    return [workbook_path(sport, date)] if sport else []


@contextmanager
def task_workbook_locks(task: str):
    paths = task_workbook_paths(task)
    stack = []
    try:
        for path in sorted(paths, key=lambda p: str(p)):
            cm = workbook_file_lock(path)
            cm.__enter__()
            stack.append(cm)
        yield
    finally:
        for cm in reversed(stack):
            cm.__exit__(None, None, None)


def run_task(task: str) -> dict[str, Any]:
    mapping = {
        "nba_daily_picks": lambda: daily_picks("nba"),
        "mlb_daily_picks": lambda: daily_picks("mlb"),
        "nba_prop_monitor": lambda: prop_monitor("nba"),
        "mlb_prop_monitor": lambda: prop_monitor("mlb"),
        "nba_injury_monitor": lambda: injury_monitor("nba"),
        "mlb_injury_monitor": lambda: injury_monitor("mlb"),
        "nba_clv_tracker": lambda: clv_tracker("nba"),
        "mlb_clv_tracker": lambda: clv_tracker("mlb"),
        "check_results": check_results,
        "game_completion_monitor": game_completion_monitor,
        "verify": verify,
    }
    if task not in mapping:
        raise SystemExit(f"Unknown task: {task}")
    return mapping[task]()


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--task")
    parser.add_argument("--test-telegram", action="store_true", help="Send a Telegram smoke-test message and exit")
    args = parser.parse_args()
    ensure_dirs()
    if args.test_telegram:
        ok = send_telegram(f"✅ sports_system_runner Telegram test {now_iso()}")
        result = {"status": "ok" if ok else "error", "task": "test_telegram", "telegram_sent": ok}
        safe_print("JSON_RESULT=" + json.dumps(result, sort_keys=True))
        return 0 if ok else 1
    if not args.task:
        parser.error("--task is required unless --test-telegram is used")
    task_start_time = time.time()
    # RES-02: sentinel set after run_task() completes; None while task is in-flight.
    # A BrokenPipeError is only reclassified as non-fatal when this is not None (D-08).
    _task_result: dict[str, Any] | None = None
    budget = TASK_TIMEOUTS.get(args.task, 60)
    old_handler = signal.signal(signal.SIGALRM, _sigalrm_handler)
    signal.alarm(budget)
    try:
        # Reset per-invocation state so each task run starts with a fresh breaker and log buffer.
        _telegram_breaker["consecutive_failures"] = 0
        _telegram_breaker["tripped"] = False
        _telegram_breaker["suppressed"] = 0
        _task_log_lines.clear()
        with LOCK_FILE.open("w") as lock:
            fcntl.flock(lock, fcntl.LOCK_EX)
            lock.write(f"pid={os.getpid()} task={args.task} acquired_at={now_iso()}\n")
            lock.flush()
            with task_workbook_locks(args.task):
                result = run_task(args.task)
        # RES-02: mark task as complete OUTSIDE the locks block so any exception inside
        # run_task() leaves _task_result as None and still fires the TASK FAILED alert.
        _task_result = result
        dispatch_alerts(args.task, result)
        safe_print("JSON_RESULT=" + json.dumps(result, sort_keys=True))
        return 0
    except TaskTimeoutError as e:
        # RES-03: distinct timeout alert — separate from ❌ SPORTS TASK FAILED (D-06).
        log(f"TIMEOUT task={args.task}: exceeded {budget}s wall-clock budget")
        send_telegram(f"⏱ TASK TIMED OUT: {args.task}\nBudget: {budget}s exceeded")
        safe_print("JSON_RESULT=" + json.dumps(
            {"status": "timeout", "task": args.task, "budget_s": budget}, sort_keys=True
        ))
        return 1
    except Exception as e:
        # RES-02: if the task completed and the pipe closed afterward, log and exit clean.
        if _task_result is not None and isinstance(e, BrokenPipeError):
            log("WARNING: BrokenPipeError after task completion — cron pipe closed; task data persisted")
            return 0
        err = {"status": "error", "task": args.task, "error": str(e), "traceback": traceback.format_exc()}
        # D-03/D-04: Additive traceback dump to robust file sink — never stdout.
        try:
            with RUN_LOG.open("a") as _tb_file:
                _tb_file.write(
                    f"[{now_iso()}] TRACEBACK task={args.task}:\n{traceback.format_exc()}\n"
                )
        except Exception:
            pass
        log(f"ERROR task={args.task}: {e}")
        send_telegram(f"❌ SPORTS TASK FAILED: {args.task}\nError: {e}")
        safe_print("JSON_RESULT=" + json.dumps(err, sort_keys=True))
        return 1
    finally:
        # RES-03: always cancel SIGALRM and restore the prior handler, even on timeout/error.
        signal.alarm(0)
        signal.signal(signal.SIGALRM, old_handler)
        elapsed = time.time() - task_start_time
        log(f"[{args.task}] completed in {elapsed:.1f}s")
        if elapsed > 90:
            log(f"WARNING: {args.task} took {elapsed:.1f}s — consider further optimization")
        # WR-01: emit the REAL suppressed count at task end so operators can see it in logs.
        # The trip-time message cannot know the count — suppression accrues after trip.
        if _telegram_breaker["tripped"] and _telegram_breaker["suppressed"]:
            log(f"Telegram breaker suppressed {_telegram_breaker['suppressed']} alerts this run — Telegram unreachable")
        # Single end-of-task Obsidian sync (D-04): fires on BOTH success and failure.
        # Uses the implemented `sports_run_log` trigger (appends to Meta/RunLog.md).
        # CR-01 fix: the prior trigger name was not wired into the handler and was silently dropped.
        try:
            task_name = getattr(args, "task", None) or "unknown"
            log_excerpt = "\n".join(_task_log_lines[-50:])
            summary_line = f"[{task_name}] completed in {round(elapsed, 1)}s\n{log_excerpt}"
            obsidian_sync({
                "trigger": "sports_run_log",
                "date": today_str(),
                "data": {"line": summary_line},
            })
        except Exception:
            pass


if __name__ == "__main__":
    raise SystemExit(main())
