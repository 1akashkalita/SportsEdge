def pick_text(row: tuple[Any, ...]) -> str:
    selection = row[7] if len(row) > 7 else ""
    pick_type = row[6] if len(row) > 6 else ""
    line = row[8] if len(row) > 8 else ""
    odds = row[9] if len(row) > 9 else ""
    platform = row[14] if len(row) > 14 else ""
    ev = row[19] if len(row) > 19 else ""
    prob = row[18] if len(row) > 18 else ""
    parts = [str(x) for x in [selection, pick_type] if x not in (None, "")]
    if line not in (None, ""):
        parts.append(f"line {line}")
    if odds not in (None, ""):
        parts.append(str(odds))
    if platform not in (None, ""):
        parts.append(f"[{platform}]")
    parts.append(ev_display(ev, "no model probability available in approved row" if prob in (None, "") else None))
    return " ".join(parts) or "No wager auto-generated"


def top_props_from_workbook(path: str, limit: int = 3) -> list[str]:
    if not path or not Path(path).exists():
        return []
    wb = safe_load_workbook(path, read_only=True, data_only=True)
    try:
        if "Player Props" not in wb.sheetnames:
            return []
        ws = wb["Player Props"]
        props = []
        for row in ws.iter_rows(min_row=2, values_only=True):
            if not row or row[10] not in ("ACTIVE", "active", None):
                continue
            player = row[3] if len(row) > 3 else ""
            stat = row[6] if len(row) > 6 else ""
            line = row[7] if len(row) > 7 else ""
            platform = row[14] if len(row) > 14 else ""
            if player and stat:
                label = f"{player} {stat} {line}"
                if platform not in (None, ""):
                    label += f" [{platform}]"
                props.append(label)
            if len(props) >= limit:
                break
        return props
    finally:
        wb.close()



