def ensure_workbook(sport: str, date: str | None = None) -> Path:
    ensure_dirs()
    path = workbook_path(sport, date)
    if path.exists():
        wb = safe_load_workbook(path)
    else:
        wb = Workbook()
        wb.remove(wb.active)
    expected = {
        "Picks": PICKS_HEADERS,
        "Player Props": PROPS_HEADERS,
        "Props": PROPS_HEADERS,
        "CLV Tracker": CLV_HEADERS,
        "Correlated Parlays": PARLAY_HEADERS,
        "Skipped Picks": SKIPPED_PICK_HEADERS,
        "Injury Baseline": INJURY_HEADERS,
        "Results": RESULT_HEADERS,
        "Slip History": SLIP_HISTORY_HEADERS,
        "Conditional Specials": CONDITIONAL_SPECIALS_HEADERS + [h for h in LINE_TIMING_FIELDS if h not in CONDITIONAL_SPECIALS_HEADERS],
        "Live Watchlist": LIVE_WATCHLIST_HEADERS,
        "Sportsbook Comparison": COMPARISON_HEADERS,
    }
    for sheet, headers in expected.items():
        if sheet not in wb.sheetnames:
            ws = wb.create_sheet(sheet)
            ws.append(headers)
        else:
            ws = wb[sheet]
            if ws.max_row == 0 or all(c.value is None for c in ws[1]):
                ws.append(headers)
            else:
                existing = [c.value for c in ws[1]]
                if existing[: len(headers)] != headers:
                    # Preserve existing data by filling blanks and appending missing headers to the right.
                    for idx, h in enumerate(headers, start=1):
                        if idx <= ws.max_column and ws.cell(1, idx).value in (None, ""):
                            ws.cell(1, idx).value = h
                    existing_after = [ws.cell(1, c).value for c in range(1, ws.max_column + 1)]
                    for h in headers:
                        if h not in existing_after:
                            ws.cell(1, ws.max_column + 1).value = h
                            existing_after.append(h)
    save_workbook_atomic(wb, path)
    return path


