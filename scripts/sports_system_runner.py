def projection_lookup(projections: dict[Any, dict[str, Any]], player: Any, stat: Any, line: Any = None, platform: Any = None) -> dict[str, Any] | None:
    player_key = normalize_player_name(player)
    stat_norm = normalize_prop_stat(stat)
    line_val = to_float(line)
    platform_key = str(platform or "").strip().lower()
    if line_val is not None:
        if platform_key:
            exact_platform = projections.get((platform_key, player_key, stat_norm, round(float(line_val), 3)))
            if exact_platform:
                return exact_platform
        exact = projections.get((player_key, stat_norm, round(float(line_val), 3)))
        if exact:
            return exact
    return projections.get((player_key, stat_norm))


