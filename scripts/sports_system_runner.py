def skipped_picks_summary_for_date(date: str) -> tuple[int, str]:
    total = wins = losses = 0
    for sport in ["nba", "mlb"]:
        path = workbook_path(sport, date)
        if not path.exists() or not workbook_is_valid(path):
            continue
        try:
            wb = safe_load_workbook(path, read_only=True, data_only=True)
            try:
                if "Skipped Picks" not in wb.sheetnames:
                    continue
                ws = wb["Skipped Picks"]
                headers = {str(ws.cell(1, c).value or "").strip(): c for c in range(1, ws.max_column + 1)}
                result_col = headers.get("Result", 7)
                for r in range(2, ws.max_row + 1):
                    if str(ws.cell(r, 1).value or "")[:10] != date:
                        continue
                    total += 1
                    result = str(ws.cell(r, result_col).value or "").upper()
                    if result in {"WIN", "WON", "W"}:
                        wins += 1
                    elif result in {"LOSS", "LOST", "L"}:
                        losses += 1
            finally:
                wb.close()
        except Exception:
            continue
    record = f"{wins}-{losses}" if wins or losses else "0-0"
    return total, record


def build_recap_alert(result: dict[str, Any]) -> str:
    bankroll = bankroll_state()
    current = bankroll.get("current_bankroll", 100)
    roi = bankroll.get("roi_percentage_current", 0)
    pnl = bankroll.get("overall_profit_loss", 0)
    skipped_total, skipped_record = skipped_picks_summary_for_date(today_str())
    graded_total = int(result.get("graded", 0) or result.get("total", 0) or 0)
    skip_denom = skipped_total + graded_total
    skip_rate = round((skipped_total / skip_denom) * 100, 1) if skip_denom else 0.0
    breakdown = result.get("platform_breakdown") or {}
    platform_line = ""
    if isinstance(breakdown, dict) and breakdown:
        parts = []
        for platform_name, stats in breakdown.items():
            if not isinstance(stats, dict):
                continue
            record = stats.get("record") or "0-0"
            platform_pnl = stats.get("pnl")
            if platform_pnl is None:
                parts.append(f"{platform_name}: {record}")
            else:
                parts.append(f"{platform_name}: {record} ({fmt_units(platform_pnl)}u)")
        if parts:
            platform_line = "By platform: " + " | ".join(parts) + "\n"
    return (
        f"📊 DAILY RECAP {today_str()}\n"
        f"NBA: {result.get('nba_record', '0-0')} | MLB: {result.get('mlb_record', '0-0')}\n"
        f"{platform_line}"
        f"Day PNL: {fmt_units(result.get('day_pnl', pnl))} units\n"
        f"Bankroll: {fmt_units(current, '100')}/100 units\n"
        f"ROI: {fmt_units(roi)}%\n"
        f"Skipped {skipped_total} picks today ({skip_rate}% skip rate)\n"
        f"Skipped picks would have gone {skipped_record} if played\n"
        f"Best: {result.get('best_pick', 'N/A')} | Worst: {result.get('worst_pick', 'N/A')}"
    )


def maybe_rate_limit_alert(result: dict[str, Any]) -> None:
    remaining = result.get("credits_remaining")
    try:
        if remaining is not None and str(remaining).lower() != "unknown" and int(remaining) < 10:
            send_telegram(f"⚠️ API RATE LIMIT WARNING\nOdds-API.io requests remaining: {remaining}")
    except Exception:
        pass


def dispatch_alerts(task: str, result: dict[str, Any]) -> None:
    if result.get("status") != "ok":
        return
    if task in ("nba_daily_picks", "mlb_daily_picks"):
        send_telegram(build_picks_alert(task.split("_", 1)[0], result))
    elif task in ("nba_prop_monitor", "mlb_prop_monitor"):
        moves = result.get("line_moves", []) or []
        if moves:
            send_telegram(build_line_move_summary_alert(task, moves))
    elif task in ("nba_injury_monitor", "mlb_injury_monitor"):
        for change in result.get("status_changes", []):
            send_telegram(build_injury_alert(change))
    elif task == "check_results":
        send_telegram(build_recap_alert(result))
    maybe_rate_limit_alert(result)


