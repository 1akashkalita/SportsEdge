def normalize_prop_stat(value: Any) -> str:
    text = str(value or "").lower().replace("+", " ").replace("_", " ").replace("-", " ")
    compact = " ".join(text.split())
    aliases = [
        ("points rebounds assists", ["pra", "pts rebs asts", "points rebounds assists"]),
        ("points rebounds", ["pts rebs", "points rebounds"]),
        ("points assists", ["pts asts", "points assists"]),
        ("rebounds assists", ["rebs asts", "rebounds assists"]),
        ("blocks steals", ["stocks", "blocks steals", "blks stls"]),
        ("threes", ["3 pointers", "three", "threes", "3pt", "3-pointers"]),
        ("points", ["points", "pts"]),
        ("rebounds", ["rebounds", "rebs"]),
        ("assists", ["assists", "asts"]),
        ("blocks", ["blocks", "blks"]),
        ("steals", ["steals", "stls"]),
        ("turnovers", ["turnovers", "tos"]),
        ("total bases", ["total bases"]),
        ("hits runs rbis", ["hits runs rbis", "hrr"]),
        ("strikeouts", ["strikeouts", "ks", "pitcher strikeouts"]),
        ("hits allowed", ["hits allowed"]),
        ("earned runs", ["earned runs"]),
        ("outs", ["outs", "pitching outs"]),
        ("hits", ["hits"]),
        ("rbis", ["rbis", "runs batted in"]),
        ("runs", ["runs scored", "runs"]),
    ]
    for canonical, needles in aliases:
        if any(needle in compact for needle in needles):
            return canonical
    return compact


def normalize_player_name(value: Any) -> str:
    return " ".join(str(value or "").replace("_", " ").lower().split())


def american_implied_prob(price: Any) -> float | None:
    odds = to_float(price)
    if odds is None or odds == 0:
        return None
    if odds > 0:
        return 100.0 / (odds + 100.0)
    return abs(odds) / (abs(odds) + 100.0)


