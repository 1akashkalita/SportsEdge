def to_float(value: Any) -> float | None:
    try:
        if value in (None, ""):
            return None
        return float(value)
    except Exception:
        return None


def probability_to_decimal(value: Any) -> float | None:
    prob = to_float(value)
    if prob is None:
        return None
    if prob > 1:
        prob = prob / 100.0
    if prob < 0 or prob > 1:
        return None
    return prob


def ev_from_probability(probability: Any) -> float | None:
    prob = probability_to_decimal(probability)
    if prob is None:
        return None
    return round(prob * 0.909 - (1 - prob), 4)


def ev_display(value: Any, unavailable_reason: str | None = None) -> str:
    ev = to_float(value)
    if ev is not None:
        return f"EV {ev:+.3f}"
    text = str(value or "").strip()
    if text:
        return f"EV {text}"
    return f"EV unavailable ({unavailable_reason or 'probability unavailable'})"


