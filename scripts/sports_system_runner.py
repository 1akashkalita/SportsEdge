    for row in idx.values():
        row_team = str(row.get("team") or row.get("Team") or "")
        status = str(row.get("current_status") or row.get("Current Status") or row.get("status") or "UNKNOWN").upper()
        player = row.get("player") or row.get("Player Name") or "Unknown"
        if row_team in aliases or row_team.lower() in aliases:
            if any(x in status for x in bad_status):
                factors.append(f"{player} {status}")
    return factors[:3]


