def bool_cell(value: Any) -> str:
    if value in (None, ""):
        return ""
    if isinstance(value, bool):
        return "TRUE" if value else "FALSE"
    text = str(value).strip()
    if text.lower() in {"true", "yes", "1", "y"}:
        return "TRUE"
    if text.lower() in {"false", "no", "0", "n"}:
        return "FALSE"
    return text


def infer_edge_tags_from_reasoning(reasoning: Any) -> str:
    text = str(reasoning or "").lower()
    tags: list[str] = []
    if "value" in text or "+ev" in text or "positive ev" in text:
        tags.append("Value")
    if "line move" in text or "moved" in text or "dispersion" in text or "0.5+" in text or "1+ point" in text:
        tags.append("Line Movement")
    if "fade" in text or "public lean" in text:
        tags.append("Sharp Fade")
    if "injury" in text or "questionable" in text or "out" in text or "minutes risk" in text:
        tags.append("Injury Spot")
    if "matchup" in text or "opponent" in text:
        tags.append("Matchup Exploit")
    if "trend" in text or "trending" in text:
        tags.append("Trend")
    if "correlated" in text or "correlation" in text or "stack" in text or "parlay" in text:
        tags.append("Correlated")
    return "; ".join(dict.fromkeys(tags))


def parse_confidence_from_reasoning(reasoning: Any) -> str:
    match = re.search(r"\bconfidence\s*=\s*([ABC])\b", str(reasoning or ""), re.I)
    return match.group(1).upper() if match else ""


def line_movement_amount(opening: Any, closing: Any) -> float | None:
    o = to_float(opening)
    c = to_float(closing)
    if o is None or c is None:
        return None
    return round(c - o, 3)


