def obsidian_rows_from_workbook(sport: str, path: str | Path, date: str | None = None) -> dict[str, list[dict[str, Any]]]:
    date = date or today_str()
    result = {"picks": [], "props": [], "parlays": [], "injuries": [], "results": [], "sportsbook_comparison": []}
    if not path or not Path(path).exists():
        return result
    wb = safe_load_workbook(path, read_only=True, data_only=True)
    try:
        for sheet, key in [("Picks", "picks"), ("Props", "props"), ("Correlated Parlays", "parlays"), ("Injury Baseline", "injuries"), ("Results", "results"), ("Sportsbook Comparison", "sportsbook_comparison")]:
            if sheet not in wb.sheetnames:
                continue
            ws = wb[sheet]
            headers = [c.value for c in next(ws.iter_rows(min_row=1, max_row=1))]
            for row in ws.iter_rows(min_row=2, values_only=True):
                if not row or str(row[0] or "")[:10] != date:
                    continue
                result[key].append(dict(zip(headers, row)))
    finally:
        wb.close()
    return result


def obsidian_active_picks_markdown(sport: str, rows: dict[str, list[dict[str, Any]]], date: str | None = None) -> str:
    date = date or today_str()
    picks = rows.get("picks", [])
    approved = [r for r in picks if str(r.get("Confidence") or "").strip() and not str(r.get("Status") or "").upper().startswith("SKIP")]
    if not approved:
        return f"No approved {sport.upper()} picks today."
    table = "| Sport | Pick | Platform | Line | Tier | Units | EV | Probability |\n|---|---|---|---:|---|---:|---:|---:|\n"
    for r in approved:
        probability = r.get("Model Over Probability") or r.get("Probability") or ""
        try:
            probability = f"{float(probability) * 100:.1f}%" if float(probability) <= 1 else f"{float(probability):.1f}%"
        except Exception:
            probability = md_escape(probability)
        table += (
            f"| {sport.upper()} | {md_escape(r.get('Selection'))} | {md_escape(r.get('Platform') or 'DFS')} | "
            f"{md_escape(r.get('Line'))} | {md_escape(r.get('Confidence'))} | {md_escape(r.get('Units'))} | "
            f"{md_escape(ev_display(r.get('EV'), ''))} | {probability} |\n"
        )
    return table.rstrip()


