def _warn_if_rate_limit_low(headers: dict[str, Any], context: str) -> None:
    snapshot = rate_limit_snapshot(headers)
    remaining = snapshot.get("remaining")
    if remaining is None:
        return
    severity = snapshot.get("severity")
    if severity == "OK":
        return
    minutes = snapshot.get("minutes_until_reset")
    msg = (
        f"Odds-API.io rate-limit {severity.lower()} context={context} "
        f"remaining={remaining} limit={snapshot.get('limit')} reset={snapshot.get('reset')} "
        f"minutes_until_reset={round(minutes, 2) if minutes is not None else 'unknown'} "
        f"usage_pressure={round(snapshot.get('usage_pressure'), 4) if snapshot.get('usage_pressure') is not None else 'unknown'} "
        f"action={snapshot.get('action')}"
    )
    log(msg)


def _league_name_contains_nba(league: dict[str, Any]) -> bool:
    haystack = " ".join(str(league.get(k) or "") for k in ("slug", "name", "title", "key")).lower()
    if "wnba" in haystack or "women" in haystack:
        return False
    return "nba" in haystack or "national basketball association" in haystack


def _event_count_for_league(client: OddsApiIoClient, sport_key: str, slug: str, *, date: str | None = None) -> tuple[int, dict[str, Any]]:
    kwargs: dict[str, Any] = {"sport": sport_key, "league": slug, "status": "pending,live"}
    if date:
        kwargs["date"] = date
    res = client.get_events(**kwargs)
    headers = res.get("headers") or {}
    _warn_if_rate_limit_low(headers, f"league_discovery:{slug}")
    if not res.get("ok"):
        return 0, headers
    data = res.get("data") or []
    return (len(data) if isinstance(data, list) else 0), headers


def resolve_odds_api_io_league(client: OddsApiIoClient, sport: str) -> tuple[str | None, dict[str, Any]]:
    """Resolve league slug without hardcoding NBA to playoffs only.

    For NBA, discover basketball leagues, prefer candidate slugs with current/upcoming events,
    and support both regular season (usa-nba) and playoffs (usa-nba-playoffs). Env override
    is honored when it has events; otherwise discovery fallback selects the best active slug.
    """
    sport_key = SPORT_KEYS[sport]
    configured = LEAGUE_KEYS.get(sport)
    report: dict[str, Any] = {"configured": configured, "candidates": [], "selected": configured, "reason": "configured_or_default"}
    if sport != "nba":
        return configured, report

    candidates: list[str] = []
    leagues_res = client.get_leagues(sport_key)
    _warn_if_rate_limit_low(leagues_res.get("headers") or {}, "league_discovery:nba_leagues")
    if leagues_res.get("ok"):
        for league in leagues_res.get("data") or []:
            if isinstance(league, dict) and _league_name_contains_nba(league):
                slug = str(league.get("slug") or league.get("key") or "").strip()
                if slug and slug not in candidates:
                    candidates.append(slug)
    for slug in NBA_LEAGUE_FALLBACK_CANDIDATES:
        if slug not in candidates:
            candidates.append(slug)
    if configured and configured not in candidates:
        candidates.insert(0, configured)

    scored: list[dict[str, Any]] = []
    for slug in candidates[:8]:
        today_count, _ = _event_count_for_league(client, sport_key, slug, date=today_str())
        upcoming_count = today_count
        if today_count == 0:
            upcoming_count, _ = _event_count_for_league(client, sport_key, slug, date=None)
        scored.append({"slug": slug, "today_events": today_count, "upcoming_or_current_events": upcoming_count})
        if configured and slug == configured and (today_count or upcoming_count):
            report.update({"candidates": scored, "selected": slug, "reason": "configured league has current/upcoming events"})
            return slug, report
    live = [c for c in scored if c["today_events"] > 0]
    upcoming = [c for c in scored if c["upcoming_or_current_events"] > 0]
    selected_row = (live or upcoming or scored or [{"slug": configured or "usa-nba-playoffs"}])[0]
    reason = "selected NBA candidate with today's events" if live else "selected NBA candidate with upcoming/current events" if upcoming else "fallback candidate; no NBA events returned during discovery"
    report.update({"candidates": scored, "selected": selected_row["slug"], "reason": reason})
    log(f"Odds-API.io NBA league selection selected={report['selected']} reason={report['reason']} candidates={scored}")
    return selected_row["slug"], report


def odds_api_io_game_markets(
    sport: str,
    markets: str = "h2h,spreads,totals",
    retries: int = 3,
    backoff: int = 10,
) -> tuple[list[dict[str, Any]] | None, dict[str, str]]:
    """Fetch game-market odds from Odds-API.io using /odds/multi batching.

    Returns The-Odds-API-like game dictionaries for existing runner logic, but
    routes only to Odds-API.io game-market endpoints. Player-prop markets are
    blocked by the client and never routed here.
    """
    if not ENABLE_ODDS_API_IO_GAME_MARKETS:
        return [], {"credits_remaining": "DISABLED", "credits_used": "DISABLED", "status_code": "DISABLED"}
    try:
        if not os.environ.get("ODDS_API_IO_KEY"):
            key = env_value("ODDS_API_IO_KEY")
            if key:
                os.environ["ODDS_API_IO_KEY"] = key
        client = OddsApiIoClient(max_retries=retries, backoff=backoff)
        league, league_report = resolve_odds_api_io_league(client, sport)
        events_res = client.get_events(sport=SPORT_KEYS[sport], league=league, date=today_str(), status="pending,live")
        _warn_if_rate_limit_low(events_res.get("headers") or {}, f"events:{sport}")
        if not events_res.get("ok"):
            err = events_res.get("error") or {}
            log(f"Odds-API.io events error sport={sport} status={err.get('status_code')} message={err.get('message')}; continuing without game-market odds")
            return None, _headers_from_odds_io(events_res.get("headers") or {})
        events = events_res.get("data") or []
        event_ids = [e.get("id") for e in events if isinstance(e, dict) and e.get("id")]
        active_books = active_bookmakers(ODDS_API_IO_PRIMARY_BOOKMAKERS, ODDS_API_IO_MAX_ACTIVE_BOOKMAKERS)
        odds_res = client.fetch_game_market_odds_for_events(event_ids, markets=markets, bookmakers=active_books)
        _warn_if_rate_limit_low(odds_res.get("headers") or {}, f"game_markets:{sport}")
        if not odds_res.get("ok") and not odds_res.get("data"):
            err = odds_res.get("error") or {}
            log(f"Odds-API.io odds error sport={sport} error={err}; continuing without game-market odds")
            return None, _headers_from_odds_io(odds_res.get("headers") or {})
        games = odds_res.get("data") or []
        if isinstance(games, dict):
            games = [games]
        sportsbook_compared = compare_events(games, sport=sport.upper(), bookmakers=active_books)
        comparisons_by_event: dict[str, list[dict[str, Any]]] = {}
        for comp in sportsbook_compared.get("comparisons", []):
            comparisons_by_event.setdefault(str(comp.get("event_id")), []).append(comp)
        for game in games:
            game["sportsbook_comparisons"] = comparisons_by_event.get(str(game.get("id") or game.get("event_id")), [])
        diag = odds_res.get("diagnostics") or client.diagnostics
        log(
            f"Odds-API.io game markets sport={sport} events_requested={diag.get('events_requested')} "
            f"multi_calls={diag.get('odds_multi_calls')} single_calls={diag.get('single_odds_calls')} "
            f"estimated_api_calls_saved={diag.get('estimated_api_calls_saved')} "
            f"rate_limit_remaining={(odds_res.get('headers') or {}).get('x-ratelimit-remaining')}"
        )
        headers = _headers_from_odds_io(odds_res.get("headers") or {})
        headers.update({
            "events_requested": str(diag.get("events_requested", 0)),
            "odds_multi_calls": str(diag.get("odds_multi_calls", 0)),
            "single_odds_calls": str(diag.get("single_odds_calls", 0)),
            "estimated_api_calls_saved": str(diag.get("estimated_api_calls_saved", 0)),
            "selected_league": str(league or ""),
            "active_bookmakers": ",".join(active_books),
            "sportsbook_comparison_rows": str(len(sportsbook_compared.get("comparisons", []))),
            "league_selection_reason": str(league_report.get("reason", "")),
            "league_fallback_candidates": json.dumps(league_report.get("candidates", []))[:500],
        })
        return games, headers
    except Exception as e:
        msg = str(e)
        raw_key = os.environ.get("ODDS_API_IO_KEY")
        if raw_key:
            msg = msg.replace(raw_key, "[REDACTED]")
        log(f"Odds-API.io failed — continuing without book data: {msg}")
        return None, {}


