def _round_or_none(value: Any, ndigits: int = 3) -> float | None:
    val = to_float(value)
    return round(val, ndigits) if val is not None else None


def _avg(values: list[Any]) -> float | None:
    nums = [to_float(v) for v in values]
    nums = [v for v in nums if v is not None]
    return round(sum(nums) / len(nums), 3) if nums else None


def _comp_for_team(game: dict[str, Any], market: str, team: Any) -> dict[str, Any] | None:
    aliases = team_aliases(str(team or ""))
    if not aliases:
        return None
    for comp in game.get("sportsbook_comparisons") or []:
        if comp.get("market") != market:
            continue
        if team_aliases(comp.get("selection")) & aliases:
            return comp
    return None


def _comp_for_market(game: dict[str, Any], market: str, selection: str | None = None) -> dict[str, Any] | None:
    comps = [c for c in (game.get("sportsbook_comparisons") or []) if c.get("market") == market]
    if selection:
        selected = [c for c in comps if str(c.get("selection") or "").lower() == selection.lower()]
        if selected:
            return selected[0]
    return comps[0] if comps else None


def game_matches_prop(game: dict[str, Any], prop: dict[str, Any]) -> bool:
    team = prop.get("team") or prop.get("Team")
    opponent = prop.get("description") or prop.get("Opponent/Description") or prop.get("opponent")
    game_teams = team_aliases(game.get("home_team")) | team_aliases(game.get("away_team"))
    if team and not (team_aliases(team) & game_teams):
        return False
    if opponent and team_aliases(opponent) and not (team_aliases(opponent) & game_teams):
        return False
    if team and opponent and team_aliases(team) & team_aliases(opponent):
        return False
    if team:
        return bool(team_aliases(team) & game_teams)
    text = " ".join(str(prop.get(k) or "") for k in ("description", "Opponent/Description", "home_team", "away_team"))
    return bool(str(game.get("home_team") or "").lower() in text.lower() or str(game.get("away_team") or "").lower() in text.lower())


def find_game_for_prop(games: list[dict[str, Any]], prop: dict[str, Any]) -> dict[str, Any] | None:
    for game in games:
        if game_matches_prop(game, prop):
            return game
    return None


def market_movement_direction_from_context(game_total: Any, clv_total: Any, spread: Any, clv_spread: Any) -> str:
    """Describe opening/current movement only when an opening baseline is present.

    Odds-API.io game-market payloads often expose only current FanDuel/DraftKings
    lines. In that case the context is stored but movement is explicitly unknown
    rather than inferred from book disagreement.
    """
    total_now = to_float(game_total); total_open = to_float(clv_total)
    spread_now = to_float(spread); spread_open = to_float(clv_spread)
    parts = []
    if total_now is not None and total_open is not None and abs(total_now - total_open) > 1e-9:
        parts.append("total_rising" if total_now > total_open else "total_falling")
    if spread_now is not None and spread_open is not None and abs(spread_now - spread_open) > 1e-9:
        parts.append("spread_toward_team" if abs(spread_now) < abs(spread_open) else "spread_away_from_team")
    return ";".join(parts) if parts else "UNKNOWN_OPENING_LINE"


def build_prop_market_context(prop: dict[str, Any], games: list[dict[str, Any]]) -> dict[str, Any]:
    """Build research-only sportsbook game-market features for a DFS prop.

    This uses only Odds-API.io root game markets already loaded into `games`:
    h2h/moneyline, spreads, and totals. It does not fetch or verify player props,
    and callers must not use these fields in projection/tier/gate decisions yet.
    """
    empty = {field: None for field in MARKET_CONTEXT_FIELDS}
    empty.update({"market_context_available": False, "market_context_source": "UNAVAILABLE"})
    game = find_game_for_prop(games, prop)
    if not game:
        return empty
    team = prop.get("team") or prop.get("Team")
    total_comp = _comp_for_market(game, "totals", "Over") or _comp_for_market(game, "totals")
    spread_comp = _comp_for_team(game, "spreads", team)
    moneyline_comp = _comp_for_team(game, "h2h", team)
    fd_total = _round_or_none((total_comp or {}).get("fanduel_line"))
    dk_total = _round_or_none((total_comp or {}).get("draftkings_line"))
    fd_spread = _round_or_none((spread_comp or {}).get("fanduel_line"))
    dk_spread = _round_or_none((spread_comp or {}).get("draftkings_line"))
    game_total = _avg([fd_total, dk_total]) or _round_or_none((total_comp or {}).get("best_line_by_selection"))
    spread = _avg([fd_spread, dk_spread]) or _round_or_none((spread_comp or {}).get("best_line_by_selection"))
    team_implied = opponent_implied = None
    if game_total is not None and spread is not None:
        team_implied = round((game_total / 2.0) - (spread / 2.0), 3)
        opponent_implied = round(game_total - team_implied, 3)
    money_prob = None
    if moneyline_comp:
        raw_probs = [r.get("implied_probability") for r in (moneyline_comp.get("raw_rows") or []) if team_aliases(r.get("selection")) & team_aliases(team)]
        nums = [to_float(x) for x in raw_probs]
        nums = [x for x in nums if x is not None]
        money_prob = round(max(nums), 6) if nums else None
    total_disagreement = _round_or_none((total_comp or {}).get("line_difference"))
    spread_disagreement = _round_or_none((spread_comp or {}).get("line_difference"))
    clv_total = _round_or_none((total_comp or {}).get("clv_baseline_line"))
    clv_spread = _round_or_none((spread_comp or {}).get("clv_baseline_line"))
    available = bool(total_comp or spread_comp or moneyline_comp)
    return {
        "game_total": game_total,
        "spread": spread,
        "team_implied_total": team_implied,
        "opponent_implied_total": opponent_implied,
        "moneyline_implied_probability": money_prob,
        "fd_total": fd_total,
        "dk_total": dk_total,
        "fd_spread": fd_spread,
        "dk_spread": dk_spread,
        "total_disagreement": total_disagreement,
        "spread_disagreement": spread_disagreement,
        "market_movement_direction": market_movement_direction_from_context(game_total, clv_total, spread, clv_spread),
        "clv_baseline_total": clv_total,
        "clv_baseline_spread": clv_spread,
        "market_context_available": available,
        "market_context_source": "Odds-API.io FanDuel/DraftKings game markets" if available else "UNAVAILABLE",
    }


def all_market_points(game: dict[str, Any], market_key: str, outcome_name: str | None = None) -> list[float]:
    points: list[float] = []
    for book in game.get("bookmakers", []) or []:
        for market in book.get("markets", []) or []:
            if market.get("key") != market_key:
                continue
            for outcome in market.get("outcomes", []) or []:
                if outcome_name and outcome.get("name") != outcome_name:
                    continue
                val = to_float(outcome.get("point"))
                if val is not None:
                    points.append(val)
    return points


def h2h_favorite(game: dict[str, Any]) -> str | None:
    market = game_market(game, "h2h")
    if not market:
        return None
    priced = [(to_float(o.get("price")), o.get("name")) for o in market.get("outcomes", [])]
    priced = [(p, n) for p, n in priced if p is not None and n]
    if not priced:
        return None
    return min(priced, key=lambda x: x[0])[1]


def injury_index(injuries: list[dict[str, Any]] | dict[str, Any]) -> dict[str, dict[str, Any]]:
    if isinstance(injuries, dict):
        return injuries
    idx: dict[str, dict[str, Any]] = {}
    for row in injuries:
        player = str(row.get("player") or row.get("Player Name") or "").strip()
        if not player:
            continue
        idx[player.lower()] = row
    return idx


def team_injury_factors(team: str | None, injuries: list[dict[str, Any]] | dict[str, Any]) -> list[str]:
    aliases = team_aliases(team)
    idx = injury_index(injuries)
    factors = []
