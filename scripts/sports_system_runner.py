def confidence_from_score(score: int) -> str:
    if score >= 3:
        return "A"
    if score >= 2:
        return "B"
    return "C"


def units_for_conf(conf: str) -> float:
    return {"A": 3.0, "B": 2.0, "C": 1.0}.get(conf, 1.0)


def add_candidate(candidates: list[dict[str, Any]], pick: dict[str, Any]) -> None:
    pick["confidence"] = confidence_from_score(int(pick.get("score", 1)))
    pick["units"] = units_for_conf(pick["confidence"])
    candidates.append(pick)


