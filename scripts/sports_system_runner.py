        f"B-Tier: {', '.join(b_tier) if b_tier else 'None'}\n"
        f"Props: {', '.join(props) if props else 'None'}\n"
        f"Skipped {result.get('skipped_picks', 0)} picks today ({result.get('skip_rate_pct', 0)}% skip rate)\n"
        f"Skipped picks would have gone 0-0 if played (pending grading)\n"
