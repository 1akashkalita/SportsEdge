def run_injury_impact_adjustment(change: dict[str, Any], sport: str, date: str, workbook: Path) -> dict[str, Any] | None:
    """Invoke calculate_injury_impact for key NBA OUT/DOUBTFUL status changes."""
    if sport != "nba" or change.get("new_status") not in ("OUT", "DOUBTFUL", "IR"):
        return None
    script = SCRIPTS / "calculate_injury_impact.py"
    if not script.exists():
        log(f"calculate_injury_impact skipped: missing script {script}")
        return None
    cmd = [
        sys.executable,
        str(script),
        "--player", str(change.get("player") or ""),
        "--team", str(change.get("team") or ""),
        "--status", str(change.get("new_status") or "OUT"),
        "--date", date,
        "--workbook", str(workbook),
    ]
    try:
        proc = subprocess.run(cmd, capture_output=True, text=True, timeout=120)
        if proc.returncode != 0:
            log(f"calculate_injury_impact failed for {change.get('player')}: {proc.stderr[:300]}")
            return {"status": "error", "returncode": proc.returncode, "stderr": proc.stderr[-1000:]}
        json_start = proc.stdout.find("{")
        result = json.loads(proc.stdout[json_start:]) if json_start >= 0 else {"status": "ok", "stdout": proc.stdout[-1000:]}
        log(f"calculate_injury_impact applied for {change.get('player')}: rows_updated={result.get('applied', {}).get('rows_updated', 0)}")
        return result
    except Exception as exc:
        log(f"calculate_injury_impact exception for {change.get('player')}: {exc}")
        return {"status": "error", "error": str(exc)}


def injury_monitor(sport: str) -> dict[str, Any]:
    ensure_dirs()
    date = today_str()
    path = ensure_workbook(sport)
    wb = safe_load_workbook(path)
    injuries_ws = wb["Injury Baseline"]
    _inj_cols = ensure_ws_columns(injuries_ws, INJURY_HEADERS + ["Position", "Injury Type", "Notes", "Date Updated", "Athlete ID"])
    game_list = espn_game_list(sport, date)
    rows, raw_first5, api_status = espn_injury_rows(sport, game_list)
    known_players = [r.get("player", "") for r in rows]
    for flag in espn_injury_news_flags(sport, known_players):
        rows.append(flag)
    injury_map: dict[str, dict[str, Any]] = {str(r.get("player", "")).lower(): r for r in rows if r.get("player")}
    odds_pulled = enrich_picks_with_espn_odds(wb, sport, game_list)

    status_changes: list[dict[str, Any]] = []
    players_checked = 0
    picks_affected_total = 0
    sport_label = sport.upper()
    # Add/update every player on ESPN injury report so the baseline remains a real injury state file.
    for row in rows:
        player = str(row.get("player") or "").strip()
        if not player:
            continue
        players_checked += 1
        current = normalize_status(row.get("status"))
        r = find_injury_baseline_row(injuries_ws, player)
        is_new = r is None
        if is_new:
            injuries_ws.append([date, sport_label, player, row.get("team"), "UNKNOWN", current, row.get("source"), now_iso(), "New ESPN injury report row"])
            r = injuries_ws.max_row
        prev = normalize_status(injuries_ws.cell(r, 6).value or injuries_ws.cell(r, 5).value or "UNKNOWN")
        affected, refs, player_hit = affected_items_for_player(wb, player, row.get("team"))
        meaningful_change = (prev != current and prev != "UNKNOWN") or (is_new and player_hit) or (prev == "UNKNOWN" and current not in ("UNKNOWN", "ACTIVE") and player_hit)
        impact = "No impact detected"
        if meaningful_change:
            set_affected_statuses(wb, refs, player, current)
            picks_affected_total += len(affected)
            rec = "All related picks marked SKIP" if current in ("OUT", "DOUBTFUL", "IR") else ("Marked WATCH — monitor until tip-off" if current in ("QUESTIONABLE", "GTD") else "Back active — check PrizePicks for stale line value")
            pp_adjusted = None
            if prev in ("OUT", "DOUBTFUL", "IR") and current in ("ACTIVE", "PROBABLE"):
                pp_adjusted = extract_current_pp_line(sport, player)
            if current in ("OUT", "DOUBTFUL", "IR"):
                msg = (f"🚨 INJURY ALERT — {sport_label}\n{player} ({row.get('team') or 'Team unknown'}): {prev} → OUT\n"
                       f"Injury: {row.get('injury_type') or 'Not specified'}\nAffected picks: {', '.join(affected) if affected else 'None found'}\nAction: All related picks marked SKIP")
            elif current in ("QUESTIONABLE", "GTD"):
                msg = (f"⚠️ GTD ALERT — {sport_label}\n{player} ({row.get('team') or 'Team unknown'}): Listed as Questionable/GTD\n"
                       f"Affected picks: {', '.join(affected) if affected else 'None found'}\nAction: Marked WATCH — monitor until tip-off")
            elif current in ("ACTIVE", "PROBABLE"):
                adjusted_txt = "Yes" if pp_adjusted else ("No" if pp_adjusted is False else "Unknown")
                msg = (f"✅ PLAYER ACTIVATED — {sport_label}\n{player} ({row.get('team') or 'Team unknown'}): Back to Active\n"
                       f"DFS line adjusted: {adjusted_txt}\nIf No: potential stale line value — check DFS board")
            else:
                msg = build_injury_alert({"player": player, "old_status": prev, "new_status": current, "affected_picks": affected, "recommendation": rec})
            status_changes.append({
                "player": player,
                "team": row.get("team"),
                "old_status": prev,
                "new_status": current,
                "injury_type": row.get("injury_type"),
                "affected_picks": affected,
                "recommendation": rec,
                "alert_message": msg,
                "source": row.get("source"),
            })
            impact = f"Status change detected; affected_picks={len(affected)}"
        # Update all baseline columns.
        injuries_ws.cell(r, 1).value = date
        injuries_ws.cell(r, 2).value = sport_label
        injuries_ws.cell(r, 3).value = player
        injuries_ws.cell(r, 4).value = row.get("team")
        injuries_ws.cell(r, 5).value = current
        injuries_ws.cell(r, 6).value = current
        injuries_ws.cell(r, 7).value = row.get("source")
        injuries_ws.cell(r, 8).value = now_iso()
        injuries_ws.cell(r, 9).value = impact
        headers = {injuries_ws.cell(1, c).value: c for c in range(1, injuries_ws.max_column + 1)}
        for h, v in {
            "Position": row.get("position"), "Injury Type": row.get("injury_type"), "Notes": row.get("notes"),
            "Date Updated": row.get("date_updated"), "Athlete ID": row.get("athlete_id"),
        }.items():
            if h in headers:
                injuries_ws.cell(r, headers[h]).value = v
    save_workbook_atomic(wb, path)
    injury_impact_results = []
    for change in status_changes:
        impact_result = run_injury_impact_adjustment(change, sport, date, path)
        if impact_result:
            change["injury_impact"] = impact_result
            injury_impact_results.append(impact_result)
            if impact_result.get("alert"):
                change["alert_message"] = str(change.get("alert_message") or "") + "\n\n" + str(impact_result.get("alert"))
    obsidian_append_injury_changes(status_changes, sport, date)
    baseline_rows = max(injuries_ws.max_row - 1, 0)
    log(f"{sport}_injury_monitor — {players_checked} players checked, {len(status_changes)} status changes, {picks_affected_total} picks affected ESPN API: {api_status}")
    return {
        "status": "ok",
        "task": f"{sport}_injury_monitor",
        "players_checked": players_checked,
        "status_changes": status_changes,
        "injury_impact_results": injury_impact_results,
        "workbook": str(path),
        "raw_espn_injury_first5": raw_first5,
        "espn_odds_pulled": odds_pulled,
        "injury_baseline_row_count": baseline_rows,
        "game_list": game_list,
        "espn_api_status": api_status,
    }


def espn_player_stats_by_event(sport: str, event_id: str) -> dict[str, dict[str, float]]:
    if not event_id:
        return {}
    _group, _league, cdn = espn_sport_parts(sport)
    try:
        data = espn_json(f"https://cdn.espn.com/core/{cdn}/game", {"xhr": "1", "gameId": event_id})
    except Exception:
        return {}
    box = data.get("gamepackageJSON", {}).get("boxscore", {}) or {}
    stats: dict[str, dict[str, float]] = {}
    for team in box.get("players", []) or []:
        for group_data in team.get("statistics", []) or []:
            labels = group_data.get("keys") or group_data.get("names") or group_data.get("labels") or []
            for athlete in group_data.get("athletes", []) or []:
                a = athlete.get("athlete", {}) or {}
                name = a.get("displayName") or a.get("shortName")
                if not name:
                    continue
                values = athlete.get("stats") or []
                row = stats.setdefault(str(name).lower(), {})
                for label, value in zip(labels, values):
                    label_l = str(label).lower()
                    raw = value
                    if isinstance(raw, str) and "-" in raw and any(x in label_l for x in ["fieldgoals", "threepoint", "freethrows", "fg", "3pt", "ft"]):
                        raw = raw.split("-", 1)[0]
                    num = to_float(raw)
                    if num is not None:
                        row[label_l] = num
                # Alias names across NBA/MLB stat labels.
                alias_pairs = {
                    "points": ["points", "pts"], "rebounds": ["rebounds", "reb"], "assists": ["assists", "ast"],
                    "3-pt made": ["threepointfieldgoalsmade-threepointfieldgoalsattempted", "3pt"],
                    "hits": ["hits", "h"], "runs": ["runs", "r"], "rbis": ["rbis", "rbi"], "home runs": ["homeruns", "hr"],
                    "strikeouts": ["strikeouts", "k"], "total bases": ["totalbases", "tb"],
                }
                for canon, keys in alias_pairs.items():
                    for k in keys:
                        if k in row:
                            row[canon] = row[k]
                            break
    return stats


def espn_player_stats(sport: str, date: str, home: str | None, away: str | None) -> dict[str, dict[str, float]]:
    events = espn_game_list(sport, date)
    event_id = espn_event_for_teams(events, home, away)
    if not event_id:
        # game_list dicts are already normalized; match manually if old helper misses.
        for g in events:
            if team_aliases(home) & team_aliases(g.get("home_team")) and team_aliases(away) & team_aliases(g.get("away_team")):
                event_id = g.get("event_id")
                break
    return espn_player_stats_by_event(sport, str(event_id or ""))



