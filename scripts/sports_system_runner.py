def dfs_implied_total(props: list[dict[str, Any]], home_team: str | None, away_team: str | None) -> tuple[float | None, str]:
    tops = []
    desc = []
    for team in [home_team, away_team]:
        team_props = pp_team_props(props, team)
        scoring = [p for p in team_props if str(p.get("stat_name") or p.get("stat_type") or "").lower() == "points"]
        if not scoring:
            scoring = [p for p in team_props if "pts" in str(p.get("stat_name") or p.get("stat_type") or "").lower()]
        vals = [(to_float(p.get("line_score")), p) for p in scoring]
        vals = [(v, p) for v, p in vals if v is not None]
        if vals:
            val, prop = max(vals, key=lambda x: x[0])
            tops.append(val)
            desc.append(f"{prop.get('player_name')} {val}")
    if len(tops) < 2:
        return None, "not enough PP scorer props"
    # Pace proxy: top scorer line from each team times 4 approximates full-game scoring environment.
    return round(sum(tops) * 4, 1), "; ".join(desc)


