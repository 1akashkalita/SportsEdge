def parse_espn_injuries_payload(sport: str, payload: dict[str, Any], source: str) -> list[dict[str, Any]]:
    rows: list[dict[str, Any]] = []
    for team_block in payload.get("injuries", []) or []:
        team_name = team_block.get("displayName") or team_block.get("name")
        for inj in team_block.get("injuries", []) or []:
            athlete = inj.get("athlete", {}) or {}
            pos = athlete.get("position", {}) or {}
            team = inj.get("team", {}) or {}
            name = athlete.get("displayName") or athlete.get("shortName") or inj.get("displayName")
            if not name:
                continue
            rows.append({
                "player": name,
                "athlete_name": name,
                "athlete_id": str(athlete.get("id") or inj.get("athleteId") or ""),
                "team": team.get("displayName") or team.get("abbreviation") or team_name,
                "position": pos.get("abbreviation") or pos.get("displayName") or "",
                "status": normalize_status(inj.get("status")),
                "injury_type": (inj.get("type", {}) or {}).get("description") if isinstance(inj.get("type"), dict) else (inj.get("type") or inj.get("details") or ""),
                "notes": inj.get("shortComment") or inj.get("longComment") or inj.get("description") or "",
                "date_updated": inj.get("date") or inj.get("lastModified") or "",
                "source": source,
            })
    return rows


def espn_injury_rows(sport: str, game_list: list[dict[str, Any]]) -> tuple[list[dict[str, Any]], list[dict[str, Any]], str]:
    group, league, _cdn = espn_sport_parts(sport)
    api_status = "success"
    merged: dict[str, dict[str, Any]] = {}
    raw_first5: list[dict[str, Any]] = []
    try:
        league_payload = espn_json(f"{ESPN_SITE_BASE}/{group}/{league}/injuries")
        raw_first5 = parse_espn_injuries_payload(sport, league_payload, "ESPN League Injury Report")[:5]
        for row in parse_espn_injuries_payload(sport, league_payload, "ESPN League Injury Report"):
            merged[row["player"].lower()] = row
    except Exception as e:
        api_status = f"error: league injuries {e}"
    for game in game_list:
        for team_id in [game.get("home_team_id"), game.get("away_team_id")]:
            if not team_id:
                continue
            try:
                payload = espn_json(f"{ESPN_SITE_BASE}/{group}/{league}/teams/{team_id}/injuries")
                for row in parse_espn_injuries_payload(sport, payload, f"ESPN Team Injury Report team_id={team_id}"):
                    merged[row["player"].lower()] = row
            except Exception as e:
                api_status = api_status if api_status != "success" else f"partial: team injuries {team_id} {e}"
    return list(merged.values()), raw_first5, api_status


def espn_injury_news_flags(sport: str, known_players: list[str]) -> list[dict[str, Any]]:
    sport_name = "basketball" if sport == "nba" else "baseball"
    keywords = ["out", "doubtful", "questionable", "ruled out", "will not play", "day-to-day", "dnp", "injury", "injured"]
    flags: list[dict[str, Any]] = []
    try:
        data = espn_json(ESPN_NEWS_BASE, {"sport": sport_name, "limit": 20})
    except Exception:
        return flags
    for h in data.get("headlines", []) or []:
        headline = h.get("headline") or h.get("title") or ""
        desc = h.get("description") or ""
        blob = f"{headline} {desc}"
        low = blob.lower()
        if not any(k in low for k in keywords):
            continue
        matched = [p for p in known_players if p and p.lower() in low]
        for player in matched:
            flags.append({
                "player": player,
                "athlete_name": player,
                "team": "",
                "position": "",
                "status": "GTD",
                "injury_type": "news flag",
                "notes": headline,
                "date_updated": h.get("published") or h.get("lastModified") or "",
                "source": "ESPN News",
            })
    return flags


def ensure_ws_columns(ws, columns: list[str]) -> dict[str, int]:
    headers = [ws.cell(1, c).value for c in range(1, ws.max_column + 1)]
    for col in columns:
        if col not in headers:
            ws.cell(1, ws.max_column + 1).value = col
            headers.append(col)
    return {str(ws.cell(1, c).value): c for c in range(1, ws.max_column + 1) if ws.cell(1, c).value not in (None, "")}


def find_injury_baseline_row(ws, player: str) -> int | None:
    for r in range(2, ws.max_row + 1):
        if str(ws.cell(r, 3).value or "").strip().lower() == str(player or "").strip().lower():
            return r
    return None


def rows_today_as_dicts(ws) -> list[dict[str, Any]]:
    headers = [c.value for c in ws[1]]
    out = []
    date = today_str()
    for idx, vals in enumerate(ws.iter_rows(min_row=2, values_only=True), start=2):
        if vals and str(vals[0])[:10] == date:
            d = dict(zip(headers, vals))
            d["_row"] = idx
            out.append(d)
    return out


def affected_items_for_player(wb, player: str, team: str | None = None) -> tuple[list[str], dict[str, list[int]], bool]:
    """Return affected rows. Team-level hits are intentionally conservative.

    Full team names can match game rows. Three-letter abbreviations are only matched
    as uppercase standalone tokens to avoid false hits like POR inside 'sports'.
    The third return value indicates whether the player name itself appeared.
    """
    import re
    player_low = str(player or "").lower()
    full_team_aliases = {a.lower() for a in team_aliases(team) if len(a) > 3 and " " in a}
    abbr_aliases = {a.upper() for a in team_aliases(team) if 2 <= len(a) <= 4 and a.upper() == a}
    affected: list[str] = []
    row_refs: dict[str, list[int]] = {"Picks": [], "Props": [], "Correlated Parlays": []}
    player_hit_any = False
    for sheet in ["Picks", "Props", "Correlated Parlays"]:
        if sheet not in wb.sheetnames:
            continue
        ws = wb[sheet]
        for row in rows_today_as_dicts(ws):
            text = " ".join(str(v or "") for k, v in row.items() if not str(k).startswith("_"))
            low_text = text.lower()
            hit_player = bool(player_low and player_low in low_text)
            if hit_player:
                player_hit_any = True
            hit_team = False
            if sheet == "Picks":
                selection = str(row.get("Selection") or "")
                home = str(row.get("Home Team") or "")
                away = str(row.get("Away Team") or "")
                # Only team-specific picks are team affected; totals are not auto-skipped for generic roster injuries.
                if any(alias in low_text for alias in full_team_aliases) or any(re.search(rf"\\b{re.escape(abbr)}\\b", text) for abbr in abbr_aliases):
                    hit_team = bool(selection and (selection.lower() in home.lower() or selection.lower() in away.lower() or any(a in selection.lower() for a in full_team_aliases)))
            if hit_player or hit_team:
                label = row.get("Selection") or row.get("Player Name") or row.get("Parlay Name") or text[:80]
                affected.append(str(label))
                row_refs[sheet].append(int(row["_row"]))
    return affected[:10], row_refs, player_hit_any


def set_affected_statuses(wb, row_refs: dict[str, list[int]], player: str, status: str) -> None:
    status_norm = normalize_status(status)
