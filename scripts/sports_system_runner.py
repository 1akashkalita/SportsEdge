def normalize_status(value: Any) -> str:
    if isinstance(value, dict):
        value = value.get("description") or value.get("abbreviation") or value.get("name") or "UNKNOWN"
    s = str(value or "UNKNOWN").strip()
    if not s:
        return "UNKNOWN"
    up = s.upper().replace("GAME TIME DECISION", "GTD")
    aliases = {
        "AVAILABLE": "ACTIVE",
        "PROBABLE": "ACTIVE",
        "DAY-TO-DAY": "GTD",
        "DAY TO DAY": "GTD",
        "INJURED RESERVE": "IR",
        "O": "OUT",
        "Q": "QUESTIONABLE",
        "D": "DOUBTFUL",
        "INJURY_STATUS_OUT": "OUT",
        "INJURY_STATUS_QUESTIONABLE": "QUESTIONABLE",
        "INJURY_STATUS_DOUBTFUL": "DOUBTFUL",
    }
    return aliases.get(up, up)


def espn_json(url: str, params: dict[str, Any] | None = None) -> dict[str, Any]:
    r = requests.get(url, params=params or {}, timeout=30)
    r.raise_for_status()
    data = r.json()
    if not isinstance(data, dict):
        raise RuntimeError(f"Unexpected ESPN response type from {url}: {type(data).__name__}")
    return data


