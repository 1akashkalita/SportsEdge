def espn_event_odds_bonus(sport: str, game: dict[str, Any]) -> dict[str, Any]:
    group, league, _cdn = espn_sport_parts(sport)
    event_id = game.get("event_id")
    comp_id = game.get("competition_id") or event_id
    result = {"spread": "", "total": "", "win_probability": ""}
    if not event_id:
        return result
    try:
        data = espn_json(f"{ESPN_CORE_BASE}/{group}/leagues/{league}/events/{event_id}/competitions/{comp_id}/odds")
        odds_items = data.get("items", []) or []
        preferred = None
        for item in odds_items:
            provider = item.get("provider", {}) or {}
            pid = str(provider.get("id") or "")
            name = str(provider.get("name") or "")
            if pid in {"37", "41", "100", "200"} or "FanDuel" in name or "Draft" in name:
                preferred = item
                if pid in {"37", "41"} or "FanDuel" in name or "Draft" in name:
                    break
        preferred = preferred or (odds_items[0] if odds_items else {})
        result["spread"] = preferred.get("details") or preferred.get("spread") or ""
        result["total"] = preferred.get("overUnder") or ""
    except Exception:
        pass
    try:
        probs = espn_json(f"{ESPN_CORE_BASE}/{group}/leagues/{league}/events/{event_id}/competitions/{comp_id}/probabilities", {"limit": 500})
        items = probs.get("items", []) or []
        latest = items[-1] if items else {}
        hp = latest.get("homeWinPercentage")
        ap = latest.get("awayWinPercentage")
        if hp is not None and ap is not None:
            result["win_probability"] = f"Home {round(float(hp)*100,1)}% / Away {round(float(ap)*100,1)}%"
    except Exception:
        pass
    return result


def enrich_picks_with_espn_odds(wb, sport: str, game_list: list[dict[str, Any]]) -> bool:
    if "Picks" not in wb.sheetnames:
        return False
    ws = wb["Picks"]
    cols = ensure_ws_columns(ws, ["ESPN Spread", "ESPN Total", "Win Probability %"])
    pulled = False
    cache: dict[str, dict[str, Any]] = {}
    for game in game_list:
        key = str(game.get("event_id") or "")
        cache[key] = espn_event_odds_bonus(sport, game)
        if any(cache[key].values()):
            pulled = True
    for r in range(2, ws.max_row + 1):
        if str(ws.cell(r, 1).value or "")[:10] != today_str():
            continue
        gid = str(ws.cell(r, 3).value or "")
        matched = cache.get(gid)
        if not matched:
            row_home = ws.cell(r, 6).value
            row_away = ws.cell(r, 5).value
            for game in game_list:
                if team_aliases(row_home) & team_aliases(game.get("home_team")) and team_aliases(row_away) & team_aliases(game.get("away_team")):
                    matched = cache.get(str(game.get("event_id") or ""))
                    break
        if matched:
            ws.cell(r, cols["ESPN Spread"]).value = matched.get("spread")
            ws.cell(r, cols["ESPN Total"]).value = matched.get("total")
            ws.cell(r, cols["Win Probability %"]).value = matched.get("win_probability")
    return pulled


