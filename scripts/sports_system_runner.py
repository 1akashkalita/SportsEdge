def parse_espn_scoreboard_game(event: dict[str, Any]) -> dict[str, Any]:
    comp = (event.get("competitions") or [{}])[0] or {}
    status_type = event.get("status", {}).get("type", {}) or {}
    home = away = None
    home_score = away_score = None
    for c in comp.get("competitors", []) or []:
        team = c.get("team", {}) or {}
        name = team.get("displayName") or team.get("shortDisplayName") or team.get("name")
        if c.get("homeAway") == "home":
            home = name
            home_score = score_value(c.get("score"))
        elif c.get("homeAway") == "away":
            away = name
            away_score = score_value(c.get("score"))
    status_name = status_type.get("name") or "UNKNOWN"
    completed = bool(status_type.get("completed"))
    normalized = "final" if completed or status_name == "STATUS_FINAL" else ("live" if status_name in {"STATUS_IN_PROGRESS", "STATUS_HALFTIME"} else "scheduled")
    if status_name in {"STATUS_POSTPONED", "STATUS_SUSPENDED"}:
        normalized = "void"
    return {
        "event_id": str(event.get("id") or comp.get("id") or ""),
        "id": str(event.get("id") or comp.get("id") or ""),
        "status_name": status_name,
        "status": normalized,
        "completed": completed,
        "home_team": home,
        "away_team": away,
        "home_team_name": home,
        "away_team_name": away,
        "home_score": home_score,
        "away_score": away_score,
        "scores": [{"name": home, "score": home_score}, {"name": away, "score": away_score}],
        "note": status_type.get("detail") or status_type.get("description") or status_name,
    }


